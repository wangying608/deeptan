r"""
==============================================================================
Run03: Extract Cell-Specific DeepTAN Network
==============================================================================

Purpose
-------
Extract a cell-specific latent gene network from a cell-type fine-tuned DeepTAN
model.

Pipeline position
-----------------
Run01: user-defined scRNA files -> LitData
Run02: LitData -> cell-type fine-tuned DeepTAN checkpoint
Run03: LitData + fine-tuned checkpoint -> cell-specific latent network

Core computation
----------------
This script keeps the original CSN extraction logic:

  1. Load the scRNA-pretrained DeepTAN model.
  2. Dynamically import the Run02 fine-tuning LightningModule class.
  3. Load the fine-tuned checkpoint.
  4. Extract node embeddings from all LitData splits: trn + val + tst.
  5. Aggregate gene embeddings across cells.
  6. Compute cosine-similarity edge weights:
       w_ij = cos_sim(mean_h_i, mean_h_j)
  7. Compute cell-specific change:
       delta_w_ij = w_ij(fine-tuned) - w_ij(pretrained)
  8. Save the full edge table, gene embeddings, cell embeddings and metadata.

The complete edge table is saved without downstream filtering. Edge filtering,
module detection and hub analysis should be performed in later downstream scripts.

Recommended single-run usage
----------------------------
python run_03_extract_cell_network_userfriendly.py \
    --config cell_network_single.yaml

Optional overrides
------------------
python run_03_extract_cell_network_userfriendly.py \
    --config cell_network_single.yaml \
    --litdata_dir /path/to/cell_litdata \
    --finetuned_ckpt /path/to/best_model.ckpt \
    --output_dir /path/to/cell_network_output

Notes for GitHub users
----------------------
The main user-facing workflow is single-run:

  litdata_dir       : output directory generated by Run01
  finetuned_ckpt    : checkpoint generated by Run02
  output_dir        : directory where Run03 outputs will be written

The legacy multi-cell-group configuration style is still supported for internal
batch experiments, but it is not required for the standard workflow.

Author: DeepTAN Cell-Specific Network Extraction Pipeline
"""

