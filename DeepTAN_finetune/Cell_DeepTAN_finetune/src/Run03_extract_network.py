def load_pretrained_for_extraction(
    ckpt_path: str,
    map_location: str = "cpu",
) -> DeepTAN:
    """Load the scRNA-pretrained DeepTAN model for network extraction."""
    model_dir = os.path.dirname(ckpt_path)
    hparams_path = os.path.join(model_dir, "version_0", "hparams.yaml")
    load_kwargs = {"map_location": map_location}
    if os.path.exists(hparams_path):
        load_kwargs["hparams_file"] = hparams_path
    model = DeepTAN.load_from_checkpoint(ckpt_path, **load_kwargs)
    model.eval()
    logger.info(f"Loaded pretrained model: {ckpt_path}")
    logger.info(
        f"  nodes={model.num_all_nodes}, "
        f"label_dim={model.output_g_label_dim}, "
        f"is_regression={model.is_regression}"
    )
    return model


def _resolve_finetune_class(config: Dict[str, Any]):
    """Dynamically import the cell fine-tuning LightningModule class."""
    module_dir = config.get("finetune_module_dir", _DEFAULT_FINETUNE_MODULE_DIR)
    module_name = config.get("finetune_module_name", "csn_finetune")
    class_name = config.get("finetune_class_name", "DeepTANFineTune")

    if module_dir and module_dir not in sys.path:
        sys.path.insert(0, module_dir)

    module = importlib.import_module(module_name)
    if not hasattr(module, class_name):
        raise AttributeError(
            f"Cannot find class '{class_name}' in module '{module_name}'. "
            f"Check finetune_module_dir/module_name/class_name in config."
        )
    return getattr(module, class_name)


def _instantiate_finetune_module(
    finetune_cls,
    pretrained_model: DeepTAN,
    ft_config: Dict[str, Any],
    group_num_classes: int,
) -> nn.Module:
    """Instantiate the cell fine-tuning module while adapting to compatible constructor signatures."""
    sig = inspect.signature(finetune_cls.__init__)
    params = set(sig.parameters.keys())

    kwargs = {
        "pretrained_model": copy.deepcopy(pretrained_model),
        "cv_weights": torch.ones(len(pretrained_model.dict_node_names)),
        "class_weights": None,
        "config": ft_config,
    }

    num_class_aliases = [
        "group_num_classes",
        "cell_group_num_classes",
        "cell_num_classes",
        "celltype_num_classes",
        "tissue_num_classes",
        "num_classes",
        "output_g_label_dim",
    ]
    for key in num_class_aliases:
        if key in params:
            kwargs[key] = group_num_classes
            break

    try:
        return finetune_cls(**kwargs)
    except TypeError as e:
        raise TypeError(
            "Failed to instantiate fine-tune module. "
            f"Tried kwargs keys={sorted(kwargs.keys())}. "
            "Check the constructor signature in csn_finetune.py."
        ) from e


def load_finetuned_for_extraction(
    pretrained_model: DeepTAN,
    ft_ckpt_path: str,
    litdata_dir: str,
    config: Dict[str, Any],
    finetune_log_dir: Optional[str] = None,
    map_location: str = "cpu",
) -> nn.Module:
    """Load the cell-type fine-tuned model for network extraction.

The loader first tries to recover the actual training configuration from finetune_metadata.pkl, then from checkpoint hyper_parameters. If neither source is available, safe inference defaults are used."""
    finetune_cls = _resolve_finetune_class(config)

    # ── Read LitData metadata ────────────────────────────────────────
    pkl_path = os.path.join(litdata_dir, const.fname.litdata_others2save_pkl)
    with open(pkl_path, "rb") as f:
        others = pickle.load(f)
    group_num_classes = others["output_g_label_dim"]

    ft_config = None

    if finetune_log_dir:
        meta_path = os.path.join(finetune_log_dir, "finetune_metadata.pkl")
        if os.path.exists(meta_path):
            with open(meta_path, "rb") as f:
                meta = pickle.load(f)
            ft_config = meta.get("config")
            if ft_config:
                logger.info("  [BUG-1] Loaded training config from finetune_metadata.pkl")

    if ft_config is None:
        ckpt_dir = os.path.dirname(ft_ckpt_path)
        meta_path = os.path.join(ckpt_dir, "finetune_metadata.pkl")
        if os.path.exists(meta_path):
            with open(meta_path, "rb") as f:
                meta = pickle.load(f)
            ft_config = meta.get("config")
            if ft_config:
                logger.info(f"  [BUG-1] Loaded training config from {meta_path}")

    if ft_config is None:
        ckpt_data = torch.load(ft_ckpt_path, map_location="cpu")
        hp = ckpt_data.get("hyper_parameters", {})
        if "config" in hp:
            ft_config = hp["config"]
            logger.info("  [BUG-1] Loaded config from checkpoint hyper_parameters")

    if ft_config is None:
        logger.warning(
            "  [BUG-1] Cannot find training config. Using safe defaults.\n"
            "  This may cause minor inconsistencies in buffer initialization,\n"
            "  but will NOT affect inference results (forward pass is config-independent)."
        )
        ft_config = {
            "base_lr": 3e-5,
            "lr_multipliers": {
                "embed": 0.0, "feature_proj": 0.1, "fusion_mlp": 0.1,
                "gat_layers": 0.2, "pooling": 0.2,
                "ge_decoder": 0.3, "g_label_predictor": 1.0,
            },
            "weight_decay": 1e-5, "warmup_epochs": 3,
            "loss_zero_coeff": 0.5, "lambda_anchor": 0.02,
            "focal_gamma": 2.0,
            "scheduler": "cosine_warm_restarts",
            "cosine_T0": 9, "cosine_T_mult": 1,
            "plateau_patience": 5, "plateau_factor": 0.5,
            "min_lr": 1e-7, "max_epochs": 50,
        }

    ft_module = _instantiate_finetune_module(
        finetune_cls=finetune_cls,
        pretrained_model=pretrained_model,
        ft_config=ft_config,
        group_num_classes=group_num_classes,
    )

    ckpt = torch.load(ft_ckpt_path, map_location=map_location)
    state_dict = ckpt["state_dict"] if "state_dict" in ckpt else ckpt

    missing, unexpected = ft_module.load_state_dict(state_dict, strict=False)
    if missing:
        real_missing = [k for k in missing if not k.startswith("_anchor_")]
        if real_missing:
            logger.warning(f"  Missing keys (non-anchor): {real_missing[:5]}...")
    if unexpected:
        logger.warning(f"  Unexpected keys: {unexpected[:5]}...")

    ft_module.eval()
    logger.info(f"Loaded fine-tuned model: {ft_ckpt_path}")
    logger.info(f"  finetune_module={finetune_cls.__module__}.{finetune_cls.__name__}")
    logger.info(f"  group_num_classes={group_num_classes}")
    return ft_module


