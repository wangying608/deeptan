def _extract_node_embeddings(
    model_amsgp: nn.Module,
    batch: GData,
    dict_node_names: Dict[str, int],
    device: torch.device = torch.device("cpu"),
) -> Dict[str, Any]:
    """Extract node embeddings from the AMSGP NodeEmbedding stack.

The original forward method is not modified. This function calls the relevant submodules directly and returns per-graph node embeddings, gene IDs, local edge indices, and node names."""
    node_embedding_layers = model_amsgp.node_embedding_layers

    # ── Parse the batch structure ─────────────────────────────────────
    node_batch = getattr(
        batch, "batch",
        torch.zeros(batch.x.size(0), dtype=torch.long, device=device),
    )
    x = batch.x.float().to(device)
    edge_index = batch.edge_index.long().to(device)
    node_batch = node_batch.to(device)

    unique_batches, inverse, counts = torch.unique(
        node_batch, return_inverse=True, return_counts=True
    )
    node_indices_list = torch.split(
        torch.arange(node_batch.size(0), device=device), counts.tolist()
    )

    # ── Parse node_names ───────────────────────────────────────────────
    node_names = batch.node_names
    if isinstance(node_names[0], list):
        node_names_flat = [n for sublist in node_names for n in sublist]
    else:
        node_names_flat = list(node_names)

    node_names_splits = []
    start_idx = 0
    for count in counts.tolist():
        node_names_splits.append(node_names_flat[start_idx:start_idx + count])
        start_idx += count

    # ── Extract embeddings graph by graph ─────────────────────────────
    results = {
        "node_embeddings": [],
        "node_gene_ids": [],
        "edge_indices_local": [],
        "node_names_per_graph": [],
    }

    for i, (node_indices, graph_node_names) in enumerate(
        zip(node_indices_list, node_names_splits)
    ):
        if node_indices.numel() == 0:
            continue

        sub_x = x[node_indices]
        sub_edge_index, _ = pyg_subgraph(
            node_indices, edge_index,
            relabel_nodes=True, num_nodes=x.size(0),
        )
        if sub_edge_index.numel() == 0:
            continue

        # ── Manually run the NodeEmbedding forward path ───────────────────
        # Filter unknown gene names to avoid KeyError
        valid_mask = [n in dict_node_names for n in graph_node_names]
        if not all(valid_mask):
            n_invalid = sum(1 for v in valid_mask if not v)
            if n_invalid > len(graph_node_names) * 0.5:
                continue  # More than half of the gene names are invalid; skip this graph
            # Keep only valid genes in this rare case
            valid_indices = [j for j, v in enumerate(valid_mask) if v]
            graph_node_names = [graph_node_names[j] for j in valid_indices]
            sub_x = sub_x[valid_indices]
            # Rebuilding edge_index is non-trivial here; skip this graph for safety
            continue

        ids = torch.tensor(
            [dict_node_names[n] for n in graph_node_names], device=device
        )
        node_embs = node_embedding_layers.embed(ids)
        x_proj = node_embedding_layers.feature_proj(sub_x.unsqueeze(-1)).squeeze(1)
        fused = torch.cat([node_embs, x_proj], dim=-1)
        fused = node_embedding_layers.fusion_mlp(fused)

        # ── Run GAT layers normally without extracting attention weights ──
        for layer in node_embedding_layers._layers:
            fused = layer(fused, sub_edge_index)

        fused = node_embedding_layers.norm(fused)

        results["node_embeddings"].append(fused.detach().cpu())
        results["node_gene_ids"].append(ids.detach().cpu())
        results["edge_indices_local"].append(sub_edge_index.detach().cpu())
        results["node_names_per_graph"].append(list(graph_node_names))

    return results


@torch.no_grad()
def extract_signals_from_dataloader(
    model: nn.Module,
    dataloaders: List,
    dict_node_names: Dict[str, int],
    device: torch.device = torch.device("cpu"),
    max_batches: Optional[int] = None,
) -> Tuple[Dict[int, List[np.ndarray]], Set[Tuple[int, int]], List[np.ndarray]]:
    """Iterate over multiple dataloaders and collect gene embeddings, edge pairs, and cell embeddings.

All train, validation, and test dataloaders can be traversed to maximize gene coverage during network extraction."""
    if hasattr(model, "amsgp"):
        amsgp = model.amsgp
    else:
        raise ValueError("Model does not have 'amsgp' attribute")

    model.eval()
    amsgp.eval()

    gene_embeddings_all: Dict[int, List[np.ndarray]] = {}
    edge_pairs_all: Set[Tuple[int, int]] = set()
    cell_embeddings_all: List[np.ndarray] = []  # [NEW] cell embedding list
    n_cells = 0
    n_batches_total = 0
    t_start = time.time()

    for loader_idx, dataloader in enumerate(dataloaders):
        loader_name = ["train", "val", "test"][loader_idx] if loader_idx < 3 else f"loader_{loader_idx}"
        n_cells_loader = 0

        for batch_idx, batch in enumerate(dataloader):
            if max_batches and n_batches_total >= max_batches:
                break

            batch = batch.to(device)

            try:
                signals = _extract_node_embeddings(
                    amsgp, batch, dict_node_names, device=device,
                )
            except Exception as e:
                logger.warning(f"  Batch {n_batches_total} extraction failed: {e}")
                continue

            # ── Collect signals for each graph / cell ─────────────────────────
            for gi in range(len(signals["node_embeddings"])):
                embs = signals["node_embeddings"][gi].numpy()
                gene_ids = signals["node_gene_ids"][gi].numpy()
                edge_index_local = signals["edge_indices_local"][gi].numpy()

                # [NEW] Cell embedding: mean pooling across all gene embeddings in this cell
                cell_emb = embs.mean(axis=0)
                cell_embeddings_all.append(cell_emb)

                # Gene embeddings; original logic is unchanged
                for ni in range(len(gene_ids)):
                    gid = int(gene_ids[ni])
                    if gid not in gene_embeddings_all:
                        gene_embeddings_all[gid] = []
                    gene_embeddings_all[gid].append(embs[ni])

                # Edge set collected from the original edge_index and canonicalized as undirected
                n_local_nodes = len(gene_ids)
                for ei in range(edge_index_local.shape[1]):
                    local_src = int(edge_index_local[0, ei])
                    local_dst = int(edge_index_local[1, ei])

                    # Boundary check
                    if local_src >= n_local_nodes or local_dst >= n_local_nodes:
                        continue

                    gsrc = int(gene_ids[local_src])
                    gdst = int(gene_ids[local_dst])

                    # Remove self-loops
                    if gsrc == gdst:
                        continue

                    # Canonical undirected storage: always store (min_id, max_id)
                    edge_pair = (min(gsrc, gdst), max(gsrc, gdst))
                    edge_pairs_all.add(edge_pair)

                n_cells += 1
                n_cells_loader += 1

            n_batches_total += 1
            if n_batches_total % 20 == 0:
                elapsed = time.time() - t_start
                logger.info(
                    f"    [{n_batches_total} batches, {n_cells} cells, "
                    f"{len(gene_embeddings_all)} genes, "
                    f"{len(edge_pairs_all)} edges, "
                    f"{elapsed:.1f}s]"
                )

        logger.info(f"    {loader_name}: {n_cells_loader} cells")

    elapsed = time.time() - t_start
    logger.info(
        f"  Extraction complete: {n_cells} cells (all splits), "
        f"{len(gene_embeddings_all)} genes, "
        f"{len(edge_pairs_all)} edges, "
        f"{len(cell_embeddings_all)} cell embeddings "
        f"({elapsed:.1f}s)"
    )

    return gene_embeddings_all, edge_pairs_all, cell_embeddings_all


