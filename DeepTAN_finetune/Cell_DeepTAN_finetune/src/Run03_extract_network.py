# 4. Signal aggregation and edge-weight calculation
# ============================================================================

def aggregate_signals(
    gene_embeddings_all: Dict[int, List[np.ndarray]],
    edge_pairs: Set[Tuple[int, int]],
    gene_id_to_name: Dict[int, str],
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Aggregate gene embeddings across cells and compute cosine-similarity edge weights.

The edge weight is defined as w_ij = cos_sim(mean_h_i, mean_h_j) on observed undirected edges."""
    # ── Mean gene embeddings ──────────────────────────────────────────
    if not gene_embeddings_all:
        logger.error("  No gene embeddings collected!")
        return np.array([]), np.array([]), np.array([])

    n_genes = max(gene_embeddings_all.keys()) + 1
    hidden_dim = 0
    for v in gene_embeddings_all.values():
        if v:
            hidden_dim = v[0].shape[0]
            break

    mean_embeddings = np.zeros((n_genes, hidden_dim), dtype=np.float32)
    gene_cell_counts = np.zeros(n_genes, dtype=np.int64)

    for gid, emb_list in gene_embeddings_all.items():
        stacked = np.stack(emb_list, axis=0)
        mean_embeddings[gid] = stacked.mean(axis=0)
        gene_cell_counts[gid] = len(emb_list)

    n_genes_observed = int((gene_cell_counts > 0).sum())
    logger.info(
        f"  Gene embeddings: {n_genes_observed}/{n_genes} genes observed, "
        f"dim={hidden_dim}"
    )

    # ── Compute cosine similarity on observed edges only ──────────────
    edge_list = sorted(edge_pairs)  # deterministic output order

    if not edge_list:
        logger.error("  No edges found!")
        return mean_embeddings, np.array([]), np.array([])

    edge_array = np.array(edge_list, dtype=np.int64)
    cos_sim = np.zeros(len(edge_array), dtype=np.float64)

    for ei in range(len(edge_array)):
        gi, gj = edge_array[ei]
        ei_emb = mean_embeddings[gi]
        ej_emb = mean_embeddings[gj]
        norm_i = np.linalg.norm(ei_emb)
        norm_j = np.linalg.norm(ej_emb)
        if norm_i > 1e-8 and norm_j > 1e-8:
            cos_sim[ei] = float(np.dot(ei_emb, ej_emb) / (norm_i * norm_j))

    # ── Distribution diagnostics ──────────────────────────────────────
    logger.info(f"  Edges: {len(edge_array)}")
    n_neg_sim = int((cos_sim < 0).sum())
    n_pos_sim = int((cos_sim > 0).sum())
    logger.info(
        f"  Cosine sim (= edge weight w) stats: "
        f"min={cos_sim.min():.4f}, max={cos_sim.max():.4f}, "
        f"mean={cos_sim.mean():.4f}, std={cos_sim.std():.4f}, "
        f"positive={n_pos_sim} ({n_pos_sim / len(cos_sim):.1%}), "
        f"negative={n_neg_sim} ({n_neg_sim / len(cos_sim):.1%})"
    )

    return mean_embeddings, edge_array, cos_sim


def compute_delta(
    w_pre: np.ndarray,
    edge_array_pre: np.ndarray,
    w_ft: np.ndarray,
    edge_array_ft: np.ndarray,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """Compute delta_w = w_ft - w_pre on the common edge set only."""
    pre_dict = {
        (int(edge_array_pre[i, 0]), int(edge_array_pre[i, 1])): float(w_pre[i])
        for i in range(len(edge_array_pre))
    }
    ft_dict = {
        (int(edge_array_ft[i, 0]), int(edge_array_ft[i, 1])): float(w_ft[i])
        for i in range(len(edge_array_ft))
    }

    common_keys = sorted(set(pre_dict.keys()) & set(ft_dict.keys()))
    if not common_keys:
        logger.error("  No common edges between pretrained and fine-tuned!")
        return np.array([]), np.array([]), np.array([]), np.array([])

    common_edges = np.array(common_keys, dtype=np.int64)
    w_pre_aligned = np.array([pre_dict[k] for k in common_keys], dtype=np.float64)
    w_ft_aligned = np.array([ft_dict[k] for k in common_keys], dtype=np.float64)
    delta_w = w_ft_aligned - w_pre_aligned

    logger.info(
        f"  Common edges: {len(common_keys)} "
        f"(pre_only={len(pre_dict) - len(common_keys)}, "
        f"ft_only={len(ft_dict) - len(common_keys)})"
    )
    logger.info(
        f"  Δw: mean={delta_w.mean():.6f}, std={delta_w.std():.6f}, "
        f"median={np.median(delta_w):.6f}"
    )
    logger.info(
        f"    positive: {(delta_w > 0).sum()} ({(delta_w > 0).sum() / len(delta_w):.1%}), "
        f"negative: {(delta_w < 0).sum()} ({(delta_w < 0).sum() / len(delta_w):.1%})"
    )

    return common_edges, delta_w, w_pre_aligned, w_ft_aligned


# ============================================================================
# 5. Network construction and output writing
# ============================================================================

def build_network(
    common_edges: np.ndarray,
    delta_w: np.ndarray,
    w_pre: np.ndarray,
    w_ft: np.ndarray,
    gene_id_to_name: Dict[int, str],
) -> Dict[str, Any]:
    """Build the complete cell-group-specific weighted network without downstream filtering.

Filtering, module detection, and hub analysis are intentionally left to downstream scripts."""
    n_edges = len(delta_w)

    # ── Complete edge table ───────────────────────────────────────────
    edge_table_rows = []
    for ei in range(n_edges):
        gi = int(common_edges[ei, 0])
        gj = int(common_edges[ei, 1])
        edge_table_rows.append({
            "gene_i_id": gi,
            "gene_j_id": gj,
            "gene_i_name": gene_id_to_name.get(gi, f"gene_{gi}"),
            "gene_j_name": gene_id_to_name.get(gj, f"gene_{gj}"),
            "w_pre": round(float(w_pre[ei]), 8),
            "w_ft": round(float(w_ft[ei]), 8),
            "delta_w": round(float(delta_w[ei]), 8),
        })

    result = {
        "edge_table": edge_table_rows,
        "n_total_edges": n_edges,
    }

    logger.info(f"  Network built: {n_edges} total edges")
    logger.info(
        f"    Δw range: [{delta_w.min():.6f}, {delta_w.max():.6f}]"
    )

    return result


def save_csn_output(
    output_dir: str,
    cell_group_name: str,
    split_id: Optional[str],
    network: Dict[str, Any],
    mean_embeddings_pre: np.ndarray,
    mean_embeddings_ft: np.ndarray,
    gene_id_to_name: Dict[int, str],
    config: Dict[str, Any],
    raw_signals: Optional[Dict] = None,
    extra_stats: Optional[Dict] = None,
    cell_embeddings_ft: Optional[np.ndarray] = None,
) -> str:
    """Save CSN edge tables, embeddings, raw signals, and metadata."""
    if bool(config.get("simple_output", False)):
        # User-facing single-run mode:
        #   output_dir/csn/
        out_dir = os.path.join(output_dir, "csn")
    elif split_id:
        # Legacy batch mode:
        #   output_dir/<cell_group>/<split>/csn/
        out_dir = os.path.join(output_dir, cell_group_name, split_id, "csn")
    else:
        # Legacy single-cell-group mode:
        #   output_dir/<cell_group>/csn/
        out_dir = os.path.join(output_dir, cell_group_name, "csn")
    os.makedirs(out_dir, exist_ok=True)

    display_name = f"{cell_group_name}/{split_id}" if split_id else cell_group_name

    # 1. Complete edge table (parquet)
    edge_df = pl.DataFrame(network["edge_table"])
    edge_df.write_parquet(os.path.join(out_dir, "csn_edge_table.parquet"))

    # 2. Gene embedding matrices
    np.save(
        os.path.join(out_dir, "mean_embeddings_pretrained.npy"),
        mean_embeddings_pre,
    )
    np.save(
        os.path.join(out_dir, "mean_embeddings_finetuned.npy"),
        mean_embeddings_ft,
    )

    # 3. [NEW] Cell embedding matrix from the fine-tuned model for UMAP visualization
    if cell_embeddings_ft is not None and len(cell_embeddings_ft) > 0:
        np.save(
            os.path.join(out_dir, "cell_embeddings_finetuned.npy"),
            cell_embeddings_ft,
        )
        logger.info(
            f"  Saved cell embeddings: {cell_embeddings_ft.shape[0]} cells, "
            f"dim={cell_embeddings_ft.shape[1]}"
        )

    # 4. Gene-name mapping
    gene_map_rows = [
        {"gene_id": gid, "gene_name": gname}
        for gid, gname in sorted(gene_id_to_name.items())
    ]
    pl.DataFrame(gene_map_rows).write_csv(
        os.path.join(out_dir, "gene_id_map.csv")
    )

    # 5. Raw signals for diagnostics and reanalysis
    if raw_signals:
        raw_dir = os.path.join(out_dir, "raw_signals")
        os.makedirs(raw_dir, exist_ok=True)
        for key, val in raw_signals.items():
            if isinstance(val, np.ndarray):
                np.save(os.path.join(raw_dir, f"{key}.npy"), val)

    # 6. Metadata
    metadata = {
        "cell_group_name": cell_group_name,
        "split_id": split_id,
        "n_total_edges": network["n_total_edges"],
        "n_genes": len(gene_id_to_name),
        "n_cells": int(cell_embeddings_ft.shape[0]) if cell_embeddings_ft is not None else 0,
        "edge_weight_method": "cosine_similarity",
        "data_scope": "all (train + val + test)",
        "entity_type": "cell_group",
        "extra_stats": extra_stats or {},
    }
    with open(os.path.join(out_dir, "csn_metadata.yaml"), "w") as f:
        yaml.dump(metadata, f, default_flow_style=False, allow_unicode=True)

    # ── Summary log ───────────────────────────────────────────────────
    logger.info(f"\n  {'─' * 50}")
    logger.info(f"  CSN Output: {display_name}")
    logger.info(f"  Directory: {out_dir}")
    logger.info(f"  {'─' * 50}")
    total_kb = 0
    for item in sorted(os.listdir(out_dir)):
        item_path = os.path.join(out_dir, item)
        if os.path.isfile(item_path):
            sz = os.path.getsize(item_path) / 1024
            total_kb += sz
            logger.info(f"    {item} ({sz:.1f} KB)")
    logger.info(f"    Total: {total_kb:.1f} KB")

    return out_dir


