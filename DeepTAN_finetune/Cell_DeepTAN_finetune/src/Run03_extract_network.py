def process_single_cell_group(
    cell_group_name: str,
    split_id: Optional[str],
    pretrained_model: DeepTAN,
    ft_ckpt_path: str,
    litdata_dir: str,
    config: Dict[str, Any],
    device: torch.device,
) -> Optional[str]:
    """Run the complete CSN extraction workflow for one cell group and optional split."""
    display_name = f"{cell_group_name}/{split_id}" if split_id else cell_group_name

    logger.info(f"\n{'=' * 70}")
    logger.info(f"  CSN EXTRACTION: {display_name}")
    logger.info(f"  Checkpoint: {ft_ckpt_path}")
    logger.info(f"  LitData: {litdata_dir}")
    logger.info(f"{'=' * 70}")

    # ── Data loading: use all splits, train + val + test ──────────────
    dm = DeepTANDataModuleLit(
        litdata_dir,
        batch_size=config["batch_size"],
        n_workers=config["n_workers"],
    )
    dm.setup()
    all_dataloaders = [
        dm.train_dataloader(),
        dm.val_dataloader(),
        dm.test_dataloader(),
    ]
    logger.info(f"  Data: using ALL splits (train + val + test)")

    dict_node_names = pretrained_model.dict_node_names
    gene_id_to_name = {v: k for k, v in dict_node_names.items()}

    # ── Infer finetune_log_dir for metadata loading ───────────────────
    finetune_log_dir = os.path.dirname(ft_ckpt_path)

    # ── Step 1a: extract signals from the pretrained model ────────────
    logger.info(f"\n  [Step 1a] Extracting embeddings from PRETRAINED model ...")
    pre_model = copy.deepcopy(pretrained_model).to(device)
    pre_model.eval()

    gene_emb_pre, edge_pairs_pre, _ = extract_signals_from_dataloader(
        pre_model, all_dataloaders, dict_node_names, device=device,
    )
    # Pretrained cell embeddings are not saved here; UMAP uses fine-tuned embeddings

    # Record the pretrained GAT hash for later verification
    pre_gat_hash = _param_hash(pre_model.amsgp.node_embedding_layers._layers)
    del pre_model
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    # ── Step 1b: extract signals from the fine-tuned model ────────────
    logger.info(f"\n  [Step 1b] Extracting embeddings from FINE-TUNED model ...")
    ft_model = load_finetuned_for_extraction(
        pretrained_model, ft_ckpt_path, litdata_dir, config,
        finetune_log_dir=finetune_log_dir,
        map_location=str(device),
    )
    ft_model = ft_model.to(device)
    ft_model.eval()

    # [BUG-3] Verify that the GAT weights differ after fine-tuning
    ft_gat_hash = _param_hash(ft_model.amsgp.node_embedding_layers._layers)
    if pre_gat_hash == ft_gat_hash:
        logger.error(
            "  ✗ CRITICAL: GAT weights are IDENTICAL!\n"
            "  The fine-tuned model's GAT layers have the same weights as "
            "the pretrained model.\n"
            "  Possible causes: (1) wrong checkpoint path, "
            "(2) load_state_dict failed silently.\n"
            "  Aborting extraction — Δ would be meaningless."
        )
        return None
    else:
        logger.success(
            f"  ✓ GAT weights verified different "
            f"(pre={pre_gat_hash}, ft={ft_gat_hash})"
        )

    gene_emb_ft, edge_pairs_ft, cell_emb_ft_list = extract_signals_from_dataloader(
        ft_model, all_dataloaders, dict_node_names, device=device,
    )

    # [NEW] Convert the cell embedding list into a numpy array
    if cell_emb_ft_list:
        cell_embeddings_ft = np.stack(cell_emb_ft_list, axis=0)
        logger.info(
            f"  Cell embeddings (fine-tuned): {cell_embeddings_ft.shape[0]} cells, "
            f"dim={cell_embeddings_ft.shape[1]}"
        )
    else:
        cell_embeddings_ft = None
        logger.warning("  No cell embeddings collected from fine-tuned model.")

    del ft_model
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    # ── Step 2: aggregate signals and compute cosine-similarity edge weights ──
    logger.info(f"\n  [Step 2] Aggregating signals ...")

    logger.info(f"  --- Pretrained ---")
    mean_emb_pre, edges_pre, w_pre = aggregate_signals(
        gene_emb_pre, edge_pairs_pre, gene_id_to_name,
    )
    logger.info(f"  --- Fine-tuned ---")
    mean_emb_ft, edges_ft, w_ft = aggregate_signals(
        gene_emb_ft, edge_pairs_ft, gene_id_to_name,
    )

    if len(edges_pre) == 0 or len(edges_ft) == 0:
        logger.error("  No edges after aggregation. Aborting.")
        return None

    # ── Step 3: compute delta weights ─────────────────────────────────
    logger.info(f"\n  [Step 3] Computing Δw ...")
    common_edges, delta_w, w_pre_aligned, w_ft_aligned = compute_delta(
        w_pre, edges_pre, w_ft, edges_ft,
    )

    if len(common_edges) == 0:
        logger.error("  No common edges found. Aborting.")
        return None

    # ── Step 4: build the CSN network ─────────────────────────────────
    logger.info(f"\n  [Step 4] Building CSN network ...")
    network = build_network(
        common_edges, delta_w, w_pre_aligned, w_ft_aligned,
        gene_id_to_name,
    )

    # ── Additional statistics ─────────────────────────────────────────
    extra_stats = {
        "delta_w_mean": round(float(delta_w.mean()), 8),
        "delta_w_std": round(float(delta_w.std()), 8),
        "delta_w_median": round(float(np.median(delta_w)), 8),
        "delta_w_positive_frac": round(
            float((delta_w > 0).sum() / len(delta_w)), 4
        ),
        "w_ft_negative_frac": round(
            float((w_ft_aligned < 0).sum() / len(w_ft_aligned)), 4
        ),
        "n_pre_edges": len(edges_pre),
        "n_ft_edges": len(edges_ft),
        "n_common_edges": len(common_edges),
        "n_cells": int(cell_embeddings_ft.shape[0]) if cell_embeddings_ft is not None else 0,
    }

    # ── Raw signals for diagnostics and reanalysis ────────────────────
    raw_signals = {
        "common_edges": common_edges,
        "sim_pre": w_pre_aligned,
        "sim_ft": w_ft_aligned,
        "delta_w": delta_w,
        "w_pre": w_pre_aligned,
        "w_ft": w_ft_aligned,
    }

    # ── Step 5: save outputs ──────────────────────────────────────────
    logger.info(f"\n  [Step 5] Saving outputs ...")
    out_dir = save_csn_output(
        output_dir=config["output_dir"],
        cell_group_name=cell_group_name,
        split_id=split_id,
        network=network,
        mean_embeddings_pre=mean_emb_pre,
        mean_embeddings_ft=mean_emb_ft,
        gene_id_to_name=gene_id_to_name,
        config=config,
        raw_signals=raw_signals,
        extra_stats=extra_stats,
        cell_embeddings_ft=cell_embeddings_ft,
    )

    logger.success(f"  ✓ CSN extraction complete for {display_name}")
    return out_dir


