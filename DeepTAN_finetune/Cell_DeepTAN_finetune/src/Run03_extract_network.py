def _param_hash(module: nn.Module) -> str:
    """Compute an MD5 hash of module parameters for weight-change verification."""
    h = hashlib.md5()
    for p in module.parameters():
        h.update(p.detach().cpu().numpy().tobytes())
    return h.hexdigest()[:16]


def _verify_gat_weights_differ(
    pre_layers: nn.Module,
    ft_layers: nn.Module,
    label: str = "",
) -> bool:
    """Verify that the fine-tuned GAT weights differ from the pretrained GAT weights.

Returns True when the weights differ, which is the expected condition after successful fine-tuning. Returns False when the weights are identical."""
    pre_hash = _param_hash(pre_layers)
    ft_hash = _param_hash(ft_layers)
    if pre_hash == ft_hash:
        logger.error(
            f"  ✗ GAT weights IDENTICAL between pretrained and fine-tuned! {label}\n"
            f"    pre_hash={pre_hash}, ft_hash={ft_hash}\n"
            f"    Possible cause: checkpoint loading failed or the checkpoint path is incorrect."
        )
        return False
    logger.success(
        f"  ✓ GAT weights verified different. {label}\n"
        f"    pre_hash={pre_hash}, ft_hash={ft_hash}"
    )
    return True


