def _discover_splits(litdata_base: str) -> List[str]:
    """Discover legacy split subdirectories using the same layout as cell fine-tuning."""
    pkl_name = const.fname.litdata_others2save_pkl
    if os.path.exists(os.path.join(litdata_base, pkl_name)):
        return []
    if not os.path.isdir(litdata_base):
        return []
    splits = []
    for entry in os.listdir(litdata_base):
        subdir = os.path.join(litdata_base, entry)
        if os.path.isdir(subdir) and os.path.exists(
            os.path.join(subdir, pkl_name)
        ):
            splits.append(entry)

    def _sort_key(s):
        try:
            return (0, int(s), s)
        except ValueError:
            return (1, 0, s)

    splits.sort(key=_sort_key)
    return splits


def _find_best_ckpt(
    cell_group_dir: str,
    split_id: Optional[str] = None,
) -> Optional[str]:
    """Find the best checkpoint in an output directory by sorting on val_loss when available."""
    search_dir = os.path.join(cell_group_dir, split_id) if split_id else cell_group_dir
    if not os.path.isdir(search_dir):
        return None

    ckpt_files = []
    for root, dirs, files in os.walk(search_dir):
        for f in files:
            if f.endswith(".ckpt"):
                ckpt_files.append(os.path.join(root, f))

    if not ckpt_files:
        return None

    def _extract_loss(path):
        name = os.path.basename(path)
        try:
            if "val_loss=" in name:
                loss_str = name.split("val_loss=")[1].split(".ckpt")[0]
                return float(loss_str)
        except (IndexError, ValueError):
            pass
        return float("inf")

    ckpt_files.sort(key=_extract_loss)
    return ckpt_files[0]


