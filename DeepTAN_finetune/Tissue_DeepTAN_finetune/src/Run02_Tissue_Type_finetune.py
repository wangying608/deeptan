# 1b. [SPLIT-1] Multi-Split Discovery
# ============================================================================

def _discover_splits(litdata_base: str) -> List[str]:
    """
    [SPLIT-1] Automatically scan child directories under a LitData parent directory as different data splits.

    Detection logic:
      - If litdata_base itself contains litdata_others2save.pkl, use legacy single-directory mode and return an empty list.
      - Otherwise, scan child directories under litdata_base and keep valid split directories containing the metadata pkl.

    Example layout for multi-split mode:
        rosette/litdata/
        ├── 42/  (trn/ val/ tst/ litdata_others2save.pkl)
        ├── 43/
        └── 44/

    Example layout for legacy/single-split mode:
        rosette/litdata/
        ├── trn/
        ├── val/
        ├── tst/
        └── litdata_others2save.pkl

    Returns:
        A naturally sorted list of split IDs. An empty list indicates legacy mode where litdata_base itself is the valid directory.
    """
    pkl_name = const.fname.litdata_others2save_pkl

    # Backward compatibility: litdata_base itself is a valid LitData directory
    if os.path.exists(os.path.join(litdata_base, pkl_name)):
        return []

    # Scan child directories
    if not os.path.isdir(litdata_base):
        return []

    splits = []
    for entry in os.listdir(litdata_base):
        subdir = os.path.join(litdata_base, entry)
        if os.path.isdir(subdir) and os.path.exists(os.path.join(subdir, pkl_name)):
            splits.append(entry)

    # Natural sorting: numeric names are sorted numerically; non-numeric names lexicographically
    def _sort_key(s):
        try:
            return (0, int(s), s)
        except ValueError:
            return (1, 0, s)

    splits.sort(key=_sort_key)

    return splits


