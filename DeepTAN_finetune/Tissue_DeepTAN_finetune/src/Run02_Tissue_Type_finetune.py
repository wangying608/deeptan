def load_pretrained_model(ckpt_path: str, map_location: str = "cpu") -> DeepTAN:
    """Load pretrained DeepTAN model."""
    model_dir = os.path.dirname(ckpt_path)
    hparams_path = os.path.join(model_dir, "version_0", "hparams.yaml")

    load_kwargs = {"map_location": map_location}
    if os.path.exists(hparams_path):
        load_kwargs["hparams_file"] = hparams_path
        logger.info(f"Loading hparams from: {hparams_path}")
    else:
        logger.warning(f"hparams.yaml not found at {hparams_path}; loading without it")

    model = DeepTAN.load_from_checkpoint(ckpt_path, **load_kwargs)
    logger.success(f"Loaded pretrained model from: {ckpt_path}")
    logger.info(f"  input_dim={model.input_dim}, "
                f"output_g_label_dim={model.output_g_label_dim}, "
                f"is_regression={model.is_regression}, "
                f"num_nodes={model.num_all_nodes}")
    return model


def finetune_single_tissue(
    tissue_name: str,
    pretrained_model: DeepTAN,
    datamodule: LightningDataModule,
    cv_weights: torch.Tensor,
    class_weights: Optional[List[float]],
    config: Dict[str, Any],
    tissue_num_classes: Optional[int] = None,
    split_id: Optional[str] = None,
) -> Tuple[str, str, Optional[List[Dict]]]:
    """
    Fine-tune on a single tissue.

    [SPLIT-2] Add split_id support:
      - If specified, outputs are written to output/<tissue>/<split_id>/
      - If None, outputs use the backward-compatible output/<tissue>/finetune_<timestamp>_<rand>/ layout.

    Returns:
        Tuple of (best_checkpoint_path, tissue_log_dir, test_results)
    """
    # [SPLIT-2] Resolve the output directory based on split_id
    if split_id is not None:
        tissue_log_dir = os.path.join(
            config["output_dir"], tissue_name, split_id,
        )
    else:
        tissue_log_dir = os.path.join(
            config["output_dir"], tissue_name,
            f"finetune_{time_string()}_{random_string(5)}",
        )
    os.makedirs(tissue_log_dir, exist_ok=True)

    display_name = f"{tissue_name}/{split_id}" if split_id else tissue_name

    logger.info(f"\n{'='*70}")
    logger.info(f"  FINE-TUNING: {display_name}")
    logger.info(f"  Output: {tissue_log_dir}")
    logger.info(f"  Tissue num_classes: {tissue_num_classes} "
                f"(pretrained: {pretrained_model.output_g_label_dim})")
    logger.info(f"{'='*70}\n")

    pretrained_copy = copy.deepcopy(pretrained_model)

    # Fine-tuning mode: use Xavier initialization for the rebuilt tissue-specific head
    ft_model = DeepTANFineTune(
        pretrained_model=pretrained_copy,
        cv_weights=cv_weights,
        class_weights=class_weights,
        config=config,
        tissue_num_classes=tissue_num_classes,
    )

    assert ft_model.verify_embed_frozen(), "Embed layer is NOT properly frozen!"
    logger.success("✓ Embed layer verified frozen")

    embed_hash_before = _param_hash(ft_model.amsgp.node_embedding_layers.embed)

    # [SPLIT-2] Include split_id in checkpoint filenames for disambiguation
    ckpt_prefix = f"{tissue_name}_{split_id}_" if split_id else f"{tissue_name}_"

    callbacks = [
        EarlyStopping(
            monitor="val/loss_unweighted",
            patience=config["es_patience"],
            mode="min",
            verbose=True,
        ),
        ModelCheckpoint(
            dirpath=tissue_log_dir,
            filename=ckpt_prefix + "epoch={epoch:02d}_val_loss={val/loss_unweighted:.4f}",
            monitor="val/loss_unweighted",
            mode="min",
            save_top_k=3,
            auto_insert_metric_name=False,
        ),
        LearningRateMonitor(logging_interval="epoch"),
    ]

    tb_logger = TensorBoardLogger(save_dir=tissue_log_dir, name="")

    trainer = Trainer(
        enable_progress_bar=True,
        accumulate_grad_batches=config["accumulate_grad_batches"],
        logger=tb_logger,
        log_every_n_steps=5,
        precision=config["precision"],
        accelerator=config["accelerator"],
        devices=config["devices"],
        max_epochs=config["max_epochs"],
        min_epochs=config["min_epochs"],
        callbacks=callbacks,
        num_sanity_val_steps=2,
        default_root_dir=tissue_log_dir,
        gradient_clip_val=config["gradient_clip_val"],
        gradient_clip_algorithm="norm",
    )

    trainer.fit(model=ft_model, datamodule=datamodule)

    embed_hash_after = _param_hash(ft_model.amsgp.node_embedding_layers.embed)
    if embed_hash_before != embed_hash_after:
        logger.error(f"CRITICAL: Embed params changed during training!")
    else:
        logger.success(f"✓ Post-training embed verification passed")

    ckpt_callback = [c for c in callbacks if isinstance(c, ModelCheckpoint)][0]
    best_ckpt = ckpt_callback.best_model_path
    best_score = ckpt_callback.best_model_score

    # Evaluate the best fine-tuned checkpoint on the test set
    test_results = None
    if best_ckpt:
        test_results = trainer.test(
            ckpt_path=best_ckpt, datamodule=datamodule
        )
    else:
        logger.warning("No best checkpoint found; testing with last model")
        test_results = trainer.test(
            model=ft_model, datamodule=datamodule
        )

    logger.success(f"  {display_name} Best val score: {best_score}")
    logger.success(f"  {display_name} Best ckpt: {best_ckpt}")

    metadata = {
        "tissue": tissue_name,
        "split_id": split_id,
        "best_ckpt": best_ckpt,
        "best_val_score": best_score.item() if best_score is not None else None,
        "config": config,
    }
    with open(os.path.join(tissue_log_dir, "finetune_metadata.pkl"), "wb") as f:
        pickle.dump(metadata, f)

    return best_ckpt, tissue_log_dir, test_results


def _param_hash(module: nn.Module) -> str:
    import hashlib
    h = hashlib.md5()
    for p in module.parameters():
        h.update(p.detach().cpu().numpy().tobytes())
    return h.hexdigest()[:16]


