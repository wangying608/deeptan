def setup_tissue_datamodule(
    tissue_config: Dict, config: Dict
) -> Tuple[LightningDataModule, Dict[str, int], Optional[int]]:
    """
    Set up LitData-based data module for a single tissue.

    [INT-1] Added pre-flight checks for litdata directory completeness.
    """
    litdata_dir = tissue_config.get("litdata")
    if not litdata_dir:
        raise ValueError("Tissue config must include 'litdata' directory path.")

    if not os.path.isdir(litdata_dir):
        raise FileNotFoundError(
            f"LitData directory does not exist: {litdata_dir}\n"
            f"Please run Run01_build_Tissue_LitData.py first."
        )

    # [INT-1] Pre-flight check: verify LitData subset completeness
    for subset in [const.dkey.abbr_train, const.dkey.abbr_val, const.dkey.abbr_test]:
        subset_dir = os.path.join(litdata_dir, subset)
        if not os.path.isdir(subset_dir):
            raise FileNotFoundError(
                f"LitData subset directory is missing: {subset_dir}\n"
                f"Please confirm that Run01 successfully converted trn/val/tst."
            )
        if len(os.listdir(subset_dir)) == 0:
            raise FileNotFoundError(
                f"LitData subset directory is empty: {subset_dir}\n"
                f"Run01 conversion may not have completed successfully."
            )

    pkl_path = os.path.join(litdata_dir, const.fname.litdata_others2save_pkl)
    if not os.path.exists(pkl_path):
        raise FileNotFoundError(
            f"litdata_others2save.pkl not found at {pkl_path}.\n"
            f"Please confirm that Run01_build_Tissue_LitData.py completed successfully."
        )

    with open(pkl_path, "rb") as f:
        others = pickle.load(f)

    dict_node_names = others["dict_node_names"]
    output_g_label_dim = others["output_g_label_dim"]

    logger.info(f"  LitData: {litdata_dir}")
    logger.info(f"  dict_node_names: {len(dict_node_names)} genes")
    logger.info(f"  output_g_label_dim: {output_g_label_dim}")

    dm = DeepTANDataModuleLit(
        litdata_dir,
        batch_size=config["batch_size"],
        n_workers=config["n_workers"],
    )
    dm.setup()
    return dm, dict_node_names, output_g_label_dim


def load_tissue_class_weights(tissue_config: Dict) -> Optional[List[float]]:
    """
    Load class weights from celltype_onehot parquet.

    [INT-2] Supports fallback: if explicit path not set, look in litdata dir.
    """
    cw_path = tissue_config.get("class_weights_parquet")

    if (not cw_path or not os.path.exists(cw_path)) and tissue_config.get("litdata"):
        fallback_path = os.path.join(
            tissue_config["litdata"], const.fname.label_class_onehot
        )
        if os.path.exists(fallback_path):
            cw_path = fallback_path
            logger.info(f"  class_weights: using {const.fname.label_class_onehot} from the LitData directory")

    if cw_path and os.path.exists(cw_path):
        df = pl.read_parquet(cw_path)
        weights = celltypes_class_weights(df)
        logger.info(f"Loaded class weights ({len(weights)} classes): "
                     f"min={min(weights):.3f}, max={max(weights):.3f}")
        return weights
    logger.warning("No class weights parquet found. Using uniform weights.")
    return None


def _resolve_cv_weights_path(tissue_config: Dict) -> str:
    """
    [INT-3] Resolve CV weights path with fallback to litdata directory.
    """
    cv_path = tissue_config.get("cv_weights", "")
    if cv_path and os.path.exists(cv_path):
        return cv_path

    litdata_dir = tissue_config.get("litdata", "")
    if litdata_dir:
        fallback = os.path.join(litdata_dir, "gene_cv_weights.csv")
        if os.path.exists(fallback):
            logger.info(f"  cv_weights: using gene_cv_weights.csv from the LitData directory")
            return fallback

    return cv_path


