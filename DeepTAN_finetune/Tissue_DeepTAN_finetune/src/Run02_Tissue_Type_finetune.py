def _process_single_split(
    tissue_name: str,
    split_id: Optional[str],
    split_tissue_cfg: Dict,
    pretrained_model: DeepTAN,
    config: Dict[str, Any],
    lambda_scan: bool,
) -> Optional[str]:
    """
    [SPLIT] Process one data split for one tissue.

    This is separated into its own function to avoid excessive nesting in main().

    Args:
        tissue_name: tissue name
        split_id: split identifier such as "42"; None indicates legacy mode
        split_tissue_cfg: tissue configuration for this split; litdata already points to the concrete subdirectory
        pretrained_model: pretrained model
        config: global configuration
        lambda_scan: whether to run lambda-scan mode

    Returns:
        best_ckpt path, or None
    """
    display_name = f"{tissue_name}/{split_id}" if split_id else tissue_name

    # Setup data
    dm, dict_node_names, output_g_label_dim = setup_tissue_datamodule(
        split_tissue_cfg, config
    )

    pretrained_label_dim = pretrained_model.output_g_label_dim
    if output_g_label_dim != pretrained_label_dim:
        logger.warning(
            f"  ⚠ {display_name}: class-count difference — "
            f"pretrained={pretrained_label_dim}, current_tissue={output_g_label_dim}. "
            f"The GLabelPredictor output layer will be rebuilt automatically."
        )
    else:
        logger.info(f"  ✓ {display_name}: class count is consistent ({output_g_label_dim})")

    # [INT-3] Load CV weights with fallback
    cv_weights_path = _resolve_cv_weights_path(split_tissue_cfg)
    cv_weights = load_cv_weights(cv_weights_path, dict_node_names)

    # [INT-2] Load class weights with fallback
    class_weights = load_tissue_class_weights(split_tissue_cfg)

    # Lambda-scan mode
    if lambda_scan:
        logger.info(f"\n  Lambda scan for {display_name}...")
        for lam in [0.005, 0.01, 0.02, 0.05]:
            scan_cfg = copy.deepcopy(config)
            scan_cfg["lambda_anchor"] = lam
            scan_cfg["max_epochs"] = 5
            scan_cfg["min_epochs"] = 5
            scan_cfg["es_patience"] = 99
            logger.info(f"  λ_anchor = {lam}")
            scan_name = f"{tissue_name}_lambda_{lam}"
            scan_split = f"{split_id}_lambda_{lam}" if split_id else None
            finetune_single_tissue(
                scan_name,
                pretrained_model, dm, cv_weights, class_weights, scan_cfg,
                tissue_num_classes=output_g_label_dim,
                split_id=scan_split,
            )
        return None

    # ── Full fine-tuning ────────────────────────────────────────────────
    best_ckpt, _tissue_log_dir, _test_results = \
        finetune_single_tissue(
            tissue_name, pretrained_model, dm, cv_weights, class_weights,
            config, tissue_num_classes=output_g_label_dim,
            split_id=split_id,
        )

    return best_ckpt


def main():

    logger.info(f"DeepTAN module path: {const.__file__}")
    parser = argparse.ArgumentParser(
        description="Run02: tissue-specific DeepTAN fine-tuning from a Run01 LitData directory."
    )
    parser.add_argument("--config", type=str, default=None,
                        help="Path to Run02 tissue fine-tuning YAML config.")
    parser.add_argument("--tissue", type=str, default=None,
                        help="Fine-tune only this tissue/run name. Default: all entries in config.tissue_order.")
    parser.add_argument("--split", type=str, default=None,
                        help="Optional split identifier for legacy multi-split LitData directories. Not needed for standard single-run use.")
    parser.add_argument("--lambda_scan", action="store_true",
                        help="Run lambda_anchor quick scan (5 epochs each)")
    args = parser.parse_args()

    config = load_config(args.config)

    ltn.seed_everything(config["seed"], workers=True)
    logger.info(f"Random seed: {config['seed']}")

    map_loc = get_map_location()
    logger.info(f"Map location: {map_loc}")

    pretrained_model = load_pretrained_model(config["pretrained_ckpt"], map_loc)


    tissue_names = [args.tissue] if args.tissue else config["tissue_order"]
    logger.info(f"Training order: {tissue_names}")

    results = {}
    for tissue_name in tissue_names:
        if tissue_name not in config["tissues"]:
            logger.error(f"Tissue '{tissue_name}' not in config. Skipping.")
            continue

        tissue_cfg = config["tissues"][tissue_name]
        litdata_base = tissue_cfg.get("litdata", "")

        # ── [SPLIT-1] Automatically discover split subdirectories ───────
        splits = _discover_splits(litdata_base)

        if splits:
            # ── Multi-split mode ─────────────────────────────────────
            # Filter: if --split is specified, run only that split
            if args.split:
                if args.split not in splits:
                    logger.error(
                        f"  specified split {args.split!r} is not among the discovered splits: {splits}"
                    )
                    continue
                splits = [args.split]

            logger.info(
                f"\n  [SPLIT] {tissue_name}: discovered {len(splits)} data splits: {splits}"
            )

            for split_id in splits:
                split_litdata = os.path.join(litdata_base, split_id)

                # Build a copy of tissue_cfg for this split; litdata points to the concrete subdirectory
                split_tissue_cfg = copy.deepcopy(tissue_cfg)
                split_tissue_cfg["litdata"] = split_litdata

                logger.info(f"\n{'─'*70}")
                logger.info(f"  [{tissue_name}] Split {split_id} — litdata: {split_litdata}")
                logger.info(f"{'─'*70}")

                best_ckpt = _process_single_split(
                    tissue_name=tissue_name,
                    split_id=split_id,
                    split_tissue_cfg=split_tissue_cfg,
                    pretrained_model=pretrained_model,
                    config=config,
                    lambda_scan=args.lambda_scan,
                )

                result_key = f"{tissue_name}/{split_id}"
                if best_ckpt:
                    results[result_key] = best_ckpt
        else:
            # ── Legacy mode: the LitData directory itself is valid; no child split directories ──
            if args.split:
                logger.warning(
                    f"  --split {args.split} was specified, but no split subdirectory was found under {litdata_base}; "
                    f"running in single-split mode."
                )

            logger.info(f"\n  [SPLIT] {tissue_name}: single-split mode (using the LitData directory directly)")

            best_ckpt = _process_single_split(
                tissue_name=tissue_name,
                split_id=None,
                split_tissue_cfg=tissue_cfg,
                pretrained_model=pretrained_model,
                config=config,
                lambda_scan=args.lambda_scan,
            )

            if best_ckpt:
                results[tissue_name] = best_ckpt

    # ── Summary ─────────────────────────────────────────────────────────
    logger.info("\n" + "=" * 70)
    logger.info("  FINE-TUNING COMPLETE")
    logger.info("=" * 70)
    for key, ckpt in results.items():
        logger.info(f"  {key}: {ckpt}")

    summary_path = os.path.join(config["output_dir"], "finetune_summary.pkl")
    os.makedirs(os.path.dirname(summary_path), exist_ok=True)
    with open(summary_path, "wb") as f:
        pickle.dump(results, f)
    logger.success(f"Summary saved to {summary_path}")



if __name__ == "__main__":
    main()
