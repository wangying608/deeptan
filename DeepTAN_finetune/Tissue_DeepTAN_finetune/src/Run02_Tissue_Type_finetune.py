# 3. Fine-Tunable DeepTAN Module (Optimized)
# ============================================================================

class DeepTANFineTune(ltn.LightningModule):
    """
    Fine-tuning wrapper for DeepTAN (Plan A1, optimized v6 + multi-split).

    This module keeps the original tissue fine-tuning behavior: reuse the
    pretrained DeepTAN backbone, adapt the classification head when needed,
    freeze the embedding layer, and optimize reconstruction, classification,
    and anchor losses.
    """

    def __init__(
        self,
        pretrained_model: DeepTAN,
        cv_weights: torch.Tensor,
        class_weights: Optional[List[float]],
        config: Dict[str, Any],
        tissue_num_classes: Optional[int] = None,
    ):
        super().__init__()
        self.save_hyperparameters(ignore=["pretrained_model", "cv_weights"])

        # ── Core model (from pretrained) ────────────────────────────────────
        self.amsgp = pretrained_model.amsgp
        self.ge_decoder = pretrained_model.ge_decoder
        self.g_label_predictor = pretrained_model.g_label_predictor

        # ── Copy essential attributes ───────────────────────────────────────
        self.dict_node_names = pretrained_model.dict_node_names
        self.all_node_names = pretrained_model.all_node_names
        self.num_all_nodes = pretrained_model.num_all_nodes
        self.input_dim = pretrained_model.input_dim
        self.is_regression = pretrained_model.is_regression

        # ── [FIX-7] Tissue-specific class-count adaptation ─────────────────
        self.pretrained_num_classes = pretrained_model.output_g_label_dim
        if tissue_num_classes is not None and tissue_num_classes != self.pretrained_num_classes:
            self.num_classes = tissue_num_classes
            self.output_g_label_dim = tissue_num_classes
            logger.warning(
                f"  [FIX-7] class-count mismatch: pretrained={self.pretrained_num_classes}, "
                f"current_tissue={tissue_num_classes}. Rebuilding the GLabelPredictor output layer."
            )
            self._adapt_g_label_predictor(
                pretrained_model, tissue_num_classes,
            )
        else:
            self.num_classes = self.pretrained_num_classes
            self.output_g_label_dim = self.pretrained_num_classes
            self._rebuilt_predictor_param_names = set()
            logger.info(
                f"  class count is consistent: {self.num_classes}; no GLabelPredictor rebuild is needed"
            )

        # ── Configuration ───────────────────────────────────────────────────
        self.config = config
        self.base_lr = config["base_lr"]
        self.lr_multipliers = config["lr_multipliers"]
        self.lambda_anchor = config["lambda_anchor"]
        self.warmup_epochs = config["warmup_epochs"]
        self.loss_zero_coeff = config["loss_zero_coeff"]

        # ── CV weights ──────────────────────────────────────────────────────
        self.register_buffer("cv_weights", cv_weights)

        # ── [FIX-3] Class weights → focal_alpha ────────────────────────────
        if class_weights is not None:
            if len(class_weights) != self.num_classes:
                logger.error(
                    f"  class_weights dimension ({len(class_weights)}) does not match num_classes "
                    f"({self.num_classes}); using uniform weights.\n"
                    f"  Please check whether the number of columns in celltype_onehot.parquet matches this tissue label set."
                )
                focal_alpha = torch.ones(self.num_classes, dtype=torch.float32)
            else:
                focal_alpha = torch.tensor(class_weights, dtype=torch.float32)
                focal_alpha = (focal_alpha + focal_alpha.mean()) / 2.0
        else:
            focal_alpha = torch.ones(self.num_classes, dtype=torch.float32)
        self.register_buffer("focal_alpha", focal_alpha)
        self.focal_gamma = config["focal_gamma"]

        # ── [FIX-6] Pre-instantiated FocalLoss ─────────────────────────────
        self.focal_loss_fn = FocalLoss(
            gamma=self.focal_gamma,
            alpha=self.focal_alpha,
            reduction="mean",
        )

        # ── [FIX-4] L2 anchor buffers ──────────────────────────────────────
        self._anchor_param_names = []
        for name, param in self.named_parameters():
            if name in self._rebuilt_predictor_param_names:
                logger.debug(f"  Skip anchor regularization for rebuilt parameter: {name}")
                continue
            buffer_name = "_anchor_" + name.replace(".", "__")
            self.register_buffer(buffer_name, param.detach().clone(), persistent=True)
            self._anchor_param_names.append((name, buffer_name))

        # ── Freeze embed layer ──────────────────────────────────────────────
        self._freeze_embed_layer()

        # ── [FIX-4] loss_smooth buffer ──────────────────────────────────────
        init_loss_smooth = 0.1
        if hasattr(pretrained_model, "loss_smooth") and pretrained_model.loss_smooth != 0.0:
            init_loss_smooth = pretrained_model.loss_smooth
        self.register_buffer("_loss_smooth", torch.tensor(init_loss_smooth, dtype=torch.float32))

        self._prev_epoch = -1
        self._init_metrics()
        self.float()

    # ── Properties ──────────────────────────────────────────────────────────

    @property
    def loss_smooth(self) -> float:
        return self._loss_smooth.item()

    @loss_smooth.setter
    def loss_smooth(self, value: float):
        self._loss_smooth.fill_(value)

    # ── Freezing ────────────────────────────────────────────────────────────

    def _freeze_embed_layer(self):
        embed_layer = self.amsgp.node_embedding_layers.embed
        for param in embed_layer.parameters():
            param.requires_grad = False
        n_frozen = sum(p.numel() for p in embed_layer.parameters())
        logger.info(f"Frozen embed layer: {n_frozen:,} parameters")

    def verify_embed_frozen(self) -> bool:
        for name, param in self.amsgp.node_embedding_layers.embed.named_parameters():
            if param.requires_grad:
                logger.error(f"ALERT: embed param '{name}' has requires_grad=True!")
                return False
        return True

    # ── [FIX-7] GLabelPredictor adaptation ──────────────────────────────────

    def _adapt_g_label_predictor(
        self,
        pretrained_model: "DeepTAN",
        tissue_num_classes: int,
    ):
        """
        [FIX-7] Rebuild GLabelPredictor output layer for tissue-specific class count.
        Preserves hidden layers and replaces only the final Linear layer.
        The new tissue-specific output layer is Xavier-initialized for normal
        fine-tuning.
        """
        predictor = self.g_label_predictor
        self._rebuilt_predictor_param_names = set()

        # ── Strategy 1: Known attribute names ────────────────────────────────
        candidate_attr_names = ["output_layer", "fc", "classifier", "head", "out_proj"]
        target_linear = None
        target_attr_name = None

        for attr_name in candidate_attr_names:
            if hasattr(predictor, attr_name):
                candidate = getattr(predictor, attr_name)
                if isinstance(candidate, nn.Linear):
                    target_linear = candidate
                    target_attr_name = attr_name
                    logger.info(
                        f"  [FIX-7] strategy 1 matched: predictor.{attr_name} = "
                        f"Linear({candidate.in_features}, {candidate.out_features})"
                    )
                    break

        # ── Strategy 2: Match by out_features in direct children ─────────────
        if target_linear is None:
            for child_name, child_module in predictor.named_children():
                if isinstance(child_module, nn.Linear):
                    if child_module.out_features == self.pretrained_num_classes:
                        target_linear = child_module
                        target_attr_name = child_name
                        logger.info(
                            f"  [FIX-7] strategy 2 matched: predictor.{child_name}"
                        )
                        break

        # ── Strategy 3: Deep search ──────────────────────────────────────────
        if target_linear is None:
            for name, module in predictor.named_modules():
                if name == "":
                    continue
                if isinstance(module, nn.Linear) and module.out_features == self.pretrained_num_classes:
                    target_linear = module
                    target_attr_name = name
            if target_linear is not None:
                logger.info(f"  [FIX-7] strategy 3 matched: predictor.{target_attr_name}")

        # ── Strategy 4: Fallback — last Linear ───────────────────────────────
        if target_linear is None:
            for name, module in predictor.named_modules():
                if name == "" or not isinstance(module, nn.Linear):
                    continue
                target_linear = module
                target_attr_name = name
            if target_linear is not None:
                logger.warning(
                    f"  [FIX-7] strategy 4 fallback: predictor.{target_attr_name}"
                )

        if target_linear is None:
            logger.error("  [FIX-7] no Linear layer found; skipping predictor adaptation.")
            self._rebuilt_predictor_param_names = set()
            return

        old_out_features = target_linear.out_features
        in_features = target_linear.in_features
        has_bias = target_linear.bias is not None

        if old_out_features != self.pretrained_num_classes:
            logger.warning(
                f"  [FIX-7] out_features={old_out_features} "
                f"≠ pretrained_num_classes={self.pretrained_num_classes}"
            )

        logger.info(
            f"  [FIX-7] replacing Linear({in_features}, {old_out_features}) "
            f"→ Linear({in_features}, {tissue_num_classes})"
        )

        # ── Create and initialize the new Linear layer ─────────────────────
        new_linear = nn.Linear(in_features, tissue_num_classes, bias=has_bias)
        nn.init.xavier_uniform_(new_linear.weight)
        if has_bias:
            nn.init.zeros_(new_linear.bias)
        init_method = "Xavier"

        # ── Replace the old layer ─────────────────────────────────────────
        parts = target_attr_name.split(".")
        if len(parts) == 1:
            setattr(predictor, parts[0], new_linear)
        else:
            parent = predictor
            for part in parts[:-1]:
                parent = parent[int(part)] if part.isdigit() else getattr(parent, part)
            last_part = parts[-1]
            if last_part.isdigit():
                parent[int(last_part)] = new_linear
            else:
                setattr(parent, last_part, new_linear)

        # ── Verify replacement ────────────────────────────────────────────
        replaced = predictor
        for part in parts:
            replaced = replaced[int(part)] if part.isdigit() else getattr(replaced, part)
        assert isinstance(replaced, nn.Linear) and replaced.out_features == tissue_num_classes

        for pname, _ in new_linear.named_parameters():
            full_name = f"g_label_predictor.{target_attr_name}.{pname}"
            self._rebuilt_predictor_param_names.add(full_name)

        logger.info(f"  [FIX-7] ✓ output layer rebuilt ({init_method})")

    # ── Forward Pass ────────────────────────────────────────────────────────

    def forward(self, batch: GData) -> Dict[str, Any]:
        node_batch = getattr(
            batch, "batch",
            torch.zeros(batch.x.size(0), dtype=torch.long, device=self.device),
        )
        if batch.x.dtype != torch.float32:
            batch.x = batch.x.float()
        if batch.edge_index.dtype != torch.long:
            batch.edge_index = batch.edge_index.long()

        z, ids = self.amsgp(
            node_names=batch.node_names,
            x=batch.x,
            edge_index=batch.edge_index,
            batch=node_batch,
        )

        recon_node_emb, pred_feat_values = self.ge_decoder(
            z, self.amsgp.node_embedding_layers.embed.weight
        )

        pred_labels = self.g_label_predictor(z)

        pred_nonzero, pred_zeros, valid_node_ids = self._create_node_masks_aligned(
            batch.node_names, pred_feat_values, ids
        )

        return {
            "embedding": z,
            "node_recon": recon_node_emb,
            "label_pred": pred_labels,
            "pred_nonzero": pred_nonzero,
            "pred_zeros": pred_zeros,
            "valid_node_ids": valid_node_ids,
            "obs_name": batch.obs_name,
            "node_names": batch.node_names,
            "ids": ids,
        }

    # ── [FIX-1] Node ordering alignment ────────────────────────────────────

    def _create_node_masks_aligned(
        self,
        batch_node_names: List[List[str]],
        pred_feat_values: torch.Tensor,
        ids: torch.Tensor,
    ) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        device = pred_feat_values.device
        counts = [len(nodes) for nodes in batch_node_names]
        cum_counts = [0] + list(accumulate(counts))
        B = pred_feat_values.shape[0]
        N_all = pred_feat_values.shape[1]

        if pred_feat_values.dim() == 3 and pred_feat_values.shape[2] == 1:
            pred_2d = pred_feat_values.squeeze(2)
        elif pred_feat_values.dim() == 3:
            pred_2d = pred_feat_values.flatten(1)
        else:
            pred_2d = pred_feat_values

        pred_valid_list = []
        for i in range(B):
            start = cum_counts[i]
            end = cum_counts[i + 1]
            node_indices = ids[start:end]
            pred_valid_list.append(pred_2d[i, node_indices])

        pred_nonzero = torch.cat(pred_valid_list)
        valid_node_ids = ids.clone()

        mask = torch.zeros(B, N_all, dtype=torch.bool, device=device)
        for i in range(B):
            start = cum_counts[i]
            end = cum_counts[i + 1]
            node_indices = ids[start:end]
            mask[i, node_indices] = True
        pred_zeros = pred_2d[~mask]

        return pred_nonzero, pred_zeros, valid_node_ids

    # ── Training / Validation / Test / Predict Steps ────────────────────────

    def training_step(self, batch: GData, batch_idx: int):
        return self._shared_step(batch, "train")

    def validation_step(self, batch: GData, batch_idx: int):
        return self._shared_step(batch, "val")

    def test_step(self, batch: GData, batch_idx: int):
        return self._shared_step(batch, "test")

    def _shared_step(self, batch: GData, stage: str) -> torch.Tensor:
        outputs = self(batch)
        losses = self._compute_losses(outputs, batch, stage)

        if batch.y is not None and not self.is_regression:
            preds = outputs["label_pred"]
            _y = batch.y if isinstance(batch.y, torch.Tensor) else torch.tensor(batch.y, device=self.device)
            if _y.ndim > 1 and _y.shape[1] > 1:
                targets_class = torch.argmax(_y, dim=1)
            else:
                targets_class = _y.long()

            self.metrics_class[f"{stage}_metrics"].update(preds, targets_class)
            probs = F.softmax(preds, dim=1)
            self.metrics_auroc[f"{stage}_metrics"].update(probs, targets_class)

        for k, v in losses.items():
            self.log(f"{stage}/{k}", v, prog_bar=(k == "loss"),
                     on_step=True, on_epoch=True, sync_dist=True,
                     batch_size=batch.num_graphs if hasattr(batch, "num_graphs") else 1)

        return losses["loss"]

    # ── Epoch-end hooks ─────────────────────────────────────────────────────

    def on_train_epoch_end(self):
        self._log_epoch_metrics("train")

    def on_validation_epoch_end(self):
        self._log_epoch_metrics("val")

    def on_test_epoch_end(self):
        self._log_epoch_metrics("test")

    def _log_epoch_metrics(self, stage: str):
        if self.trainer.sanity_checking:
            return
        bs = 1
        if not self.is_regression and f"{stage}_metrics" in self.metrics_class:
            mc = self.metrics_class[f"{stage}_metrics"].compute()
            for name, val in mc.items():
                self.log(name, val, sync_dist=True, batch_size=bs)
            self.metrics_class[f"{stage}_metrics"].reset()

            ma = self.metrics_auroc[f"{stage}_metrics"].compute()
            for name, val in ma.items():
                self.log(name, val, sync_dist=True, batch_size=bs)
            self.metrics_auroc[f"{stage}_metrics"].reset()

    # ── Loss Computation ────────────────────────────────────────────────────

    def _compute_losses(self, outputs: Dict, batch: GData, stage: str) -> Dict:
        losses = {}

        target = batch.x.squeeze(-1).float()
        pred_nonzero = outputs["pred_nonzero"]
        pred_zeros = outputs["pred_zeros"]
        valid_ids = outputs["valid_node_ids"]

        per_node_cv = self.cv_weights[valid_ids]
        mse_per_node = (pred_nonzero - target) ** 2
        cv_weighted_mse = (mse_per_node * per_node_cv).mean()
        losses["recon_MSE"] = cv_weighted_mse

        if pred_zeros.numel() > 0:
            recon_loss_zeros = F.mse_loss(pred_zeros, torch.zeros_like(pred_zeros))
        else:
            recon_loss_zeros = torch.tensor(0.0, device=self.device)
        losses["recon_zeros"] = recon_loss_zeros
        losses["recon"] = cv_weighted_mse + self.loss_zero_coeff * recon_loss_zeros

        if batch.y is None:
            losses["label"] = torch.tensor(0.0, device=self.device)
        else:
            _y = batch.y if isinstance(batch.y, torch.Tensor) else torch.tensor(batch.y, device=self.device)
            if self.is_regression:
                losses["label"] = F.mse_loss(outputs["label_pred"], _y)
            else:
                if _y.ndim > 1 and _y.shape[1] > 1:
                    _y = torch.argmax(_y, dim=1)
                losses["label"] = self.focal_loss_fn(outputs["label_pred"], _y)

        losses["anchor"] = self._compute_anchor_loss()

        total_loss, unweighted_loss = self._balance_losses(losses, stage)
        losses["loss"] = total_loss
        losses["loss_unweighted"] = unweighted_loss

        return losses

    def _compute_anchor_loss(self) -> torch.Tensor:
        if self.current_epoch < self.warmup_epochs:
            return torch.tensor(0.0, device=self.device)

        l2_sum = torch.tensor(0.0, device=self.device)
        param_count = 0

        for name, buffer_name in self._anchor_param_names:
            parts = name.split(".")
            param = self
            try:
                for part in parts:
                    param = getattr(param, part)
            except AttributeError:
                continue

            if not isinstance(param, torch.Tensor) or not param.requires_grad:
                continue

            pretrained_val = getattr(self, buffer_name)
            l2_sum = l2_sum + torch.sum((param - pretrained_val) ** 2)
            param_count += param.numel()

        if param_count > 0:
            l2_sum = l2_sum / param_count

        return self.lambda_anchor * l2_sum

    def _balance_losses(self, losses: Dict, stage: str) -> Tuple[torch.Tensor, torch.Tensor]:
        cycle_phase = self.current_epoch % 3
        anchor_loss = losses["anchor"]

        if stage == "train":
            if cycle_phase == 0:
                alpha, beta = 1.0, 0.0
            elif cycle_phase == 1:
                alpha, beta = 0.0, 1.0
            else:
                alpha, beta = 0.7, 0.3

            if beta > 0 and self.loss_smooth > 1e-8:
                scaled_label = losses["label"] * (self.loss_smooth / losses["label"].detach().clamp(min=1e-8))
            else:
                scaled_label = losses["label"]

            total_loss = alpha * losses["recon"] + beta * scaled_label + anchor_loss
            self.loss_smooth = 0.1 * total_loss.detach().item() + 0.9 * self.loss_smooth
        else:
            if self.loss_smooth > 1e-8:
                scaled_label = losses["label"] * (self.loss_smooth / losses["label"].detach().clamp(min=1e-8))
            else:
                scaled_label = losses["label"]
            total_loss = 0.5 * losses["recon"] + 0.5 * scaled_label + anchor_loss

        unweighted_loss = 0.5 * losses["recon"] + 0.5 * losses["label"]
        return total_loss, unweighted_loss

    # ── Epoch Callbacks ─────────────────────────────────────────────────────

    def on_train_epoch_start(self) -> None:
        phase_names = {0: "RECON_FOCUS", 1: "LABEL_FOCUS", 2: "JOINT(0.7/0.3)"}
        phase = self.current_epoch % 3
        logger.info(
            f"Epoch {self.current_epoch} | Phase: {phase_names[phase]} | "
            f"λ_anchor: {self.lambda_anchor} | loss_smooth: {self.loss_smooth:.4f}"
        )

    # ── Optimizer ───────────────────────────────────────────────────────────

    def configure_optimizers(self):
        param_groups = self._build_param_groups()

        optimizer = AdamW(
            param_groups,
            weight_decay=self.config["weight_decay"],
            betas=(0.9, 0.999),
            eps=1e-8,
        )

        if self.config["scheduler"] == "cosine_warm_restarts":
            scheduler = torch.optim.lr_scheduler.CosineAnnealingWarmRestarts(
                optimizer,
                T_0=self.config["cosine_T0"],
                T_mult=self.config["cosine_T_mult"],
                eta_min=self.config["min_lr"],
            )
            return {
                "optimizer": optimizer,
                "lr_scheduler": {"scheduler": scheduler, "interval": "epoch", "frequency": 1},
            }
        else:
            scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
                optimizer,
                mode="min",
                factor=self.config["plateau_factor"],
                patience=self.config["plateau_patience"],
                min_lr=self.config["min_lr"],
            )
            return {
                "optimizer": optimizer,
                "lr_scheduler": {
                    "scheduler": scheduler,
                    "monitor": "val/loss_unweighted",
                    "interval": "epoch",
                    "frequency": 1,
                    "strict": True,
                },
            }

    def _build_param_groups(self) -> List[Dict]:
        base_lr = self.base_lr
        mult = self.lr_multipliers

        embed_params = list(self.amsgp.node_embedding_layers.embed.parameters())
        feat_proj_params = list(self.amsgp.node_embedding_layers.feature_proj.parameters())
        fusion_mlp_params = list(self.amsgp.node_embedding_layers.fusion_mlp.parameters())
        gat_params = list(self.amsgp.node_embedding_layers._layers.parameters())
        norm_params = list(self.amsgp.node_embedding_layers.norm.parameters())

        pooling_params = (
            list(self.amsgp.sagpool1.parameters()) +
            list(self.amsgp.global_xgat_pool1.parameters()) +
            list(self.amsgp.global_sagpool1.parameters())
        )

        decoder_params = list(self.ge_decoder.parameters())
        predictor_params = list(self.g_label_predictor.parameters())

        groups = [
            {"params": embed_params, "lr": 0.0, "name": "embed"},
            {"params": feat_proj_params, "lr": base_lr * mult["feature_proj"], "name": "feature_proj"},
            {"params": fusion_mlp_params, "lr": base_lr * mult["fusion_mlp"], "name": "fusion_mlp"},
            {"params": gat_params + norm_params, "lr": base_lr * mult["gat_layers"], "name": "gat_layers"},
            {"params": pooling_params, "lr": base_lr * mult["pooling"], "name": "pooling"},
            {"params": decoder_params, "lr": base_lr * mult["ge_decoder"], "name": "ge_decoder"},
            {"params": predictor_params, "lr": base_lr * mult["g_label_predictor"], "name": "g_label_predictor"},
        ]

        for g in groups:
            n_total = sum(p.numel() for p in g["params"])
            n_train = sum(p.numel() for p in g["params"] if p.requires_grad)
            logger.info(f"  Param group '{g['name']}': lr={g['lr']:.2e}, "
                        f"total={n_total:,}, trainable={n_train:,}")
        return groups

    def _init_metrics(self):
        if not self.is_regression and self.num_classes is not None:
            metrics_class = MetricCollection({
                "F1_weighted": MulticlassF1Score(num_classes=self.num_classes, average="weighted"),
                "F1_macro": MulticlassF1Score(num_classes=self.num_classes, average="macro"),
                "F1_micro": MulticlassF1Score(num_classes=self.num_classes, average="micro"),
                "Accuracy": MulticlassAccuracy(num_classes=self.num_classes, average="weighted"),
                "Precision": MulticlassPrecision(num_classes=self.num_classes, average="weighted"),
                "Recall": MulticlassRecall(num_classes=self.num_classes, average="weighted"),
            })
            metrics_auroc = MetricCollection({
                "AUROC": MulticlassAUROC(
                    num_classes=self.num_classes, average="weighted", thresholds=None
                ),
            })
            self.metrics_class = nn.ModuleDict(
                {f"{k}_metrics": metrics_class.clone(prefix=f"{k}/label_")
                 for k in ["train", "val", "test"]}
            )
            self.metrics_auroc = nn.ModuleDict(
                {f"{k}_metrics": metrics_auroc.clone(prefix=f"{k}/label_")
                 for k in ["train", "val", "test"]}
            )
        else:
            self.metrics_class = nn.ModuleDict()
            self.metrics_auroc = nn.ModuleDict()


