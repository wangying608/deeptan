def load_pretrained_class_names(parquet_path: str) -> Optional[List[str]]:
    """
    [TSA-baseline] Load class names from the pretraining-stage celltype_onehot.parquet.

    Parquet layout: the first column is obs_names; remaining column names are ordered pretraining cell-class names.

    Returns:
        A list of class names in parquet-column order, or None if the file is missing or malformed.
    """
    if not parquet_path or not os.path.exists(parquet_path):
        return None
    try:
        df = pl.read_parquet(parquet_path, n_rows=0)  # Read schema only
        class_names = list(df.columns[1:])  # Skip the first obs_names column
        if len(class_names) == 0:
            logger.warning(f"  [TSA-baseline] no class columns found in parquet: {parquet_path}")
            return None
        logger.info(f"  [TSA-baseline] pretraining class names: {len(class_names)} classes "
                    f"(from {os.path.basename(parquet_path)})")
        return class_names
    except Exception as e:
        logger.warning(f"  [TSA-baseline] failed to read pretraining label file: {e}")
        return None


def load_tissue_class_names(tissue_config: Dict) -> Optional[List[str]]:
    """
    [TSA-baseline] Load class names from the fine-tuning tissue label_onehot.parquet.

    Automatically search for const.fname.label_class_onehot inside the LitData directory.

    Returns:
        A list of class names in parquet-column order, or None.
    """
    litdata_dir = tissue_config.get("litdata", "")
    if not litdata_dir:
        return None
    label_path = os.path.join(litdata_dir, const.fname.label_class_onehot)
    if not os.path.exists(label_path):
        return None
    try:
        df = pl.read_parquet(label_path, n_rows=0)
        class_names = list(df.columns[1:])
        if len(class_names) == 0:
            return None
        logger.info(f"  [TSA-baseline] tissue class names: {len(class_names)} classes "
                    f"(from {os.path.basename(label_path)})")
        return class_names
    except Exception as e:
        logger.warning(f"  [TSA-baseline] failed to read tissue label file: {e}")
        return None


