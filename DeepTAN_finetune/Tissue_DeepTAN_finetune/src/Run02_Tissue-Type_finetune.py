def load_cv_weights(cv_weights_path: str, dict_node_names: Dict[str, int]) -> torch.Tensor:
    """
    Load gene CV weights and align with dict_node_names ordering.
    CSV columns: gene, cv_weight (range [0.5, 1.0]).
    """
    num_genes = len(dict_node_names)
    weights = torch.ones(num_genes, dtype=torch.float32) * 0.75

    if not os.path.exists(cv_weights_path):
        logger.warning(f"CV weights not found: {cv_weights_path}. Using uniform 0.75.")
        return weights

    df = pl.read_csv(cv_weights_path)
    if "gene" not in df.columns or "cv_weight" not in df.columns:
        logger.warning("CV weights CSV must have 'gene' and 'cv_weight' columns.")
        return weights

    cv_dict = dict(zip(df["gene"].to_list(), df["cv_weight"].to_list()))
    matched = 0
    for gene_name, idx in dict_node_names.items():
        if gene_name in cv_dict:
            weights[idx] = cv_dict[gene_name]
            matched += 1

    logger.info(f"CV weights: matched {matched}/{num_genes} genes, "
                f"min={weights.min():.4f}, max={weights.max():.4f}, mean={weights.mean():.4f}")
    return weights


