def _create_tsa_trainer(config: Dict[str, Any]) -> Trainer:
    """
    [TSA] Create a lightweight Trainer used only for test and predict, not training.
    """
    return Trainer(
        enable_progress_bar=False,
        logger=False,
        precision=config["precision"],
        accelerator=config["accelerator"],
        devices=config["devices"],
        num_sanity_val_steps=0,
    )


def _extract_predictions(
    model: DeepTANFineTune,
    datamodule: LightningDataModule,
    config: Dict[str, Any],
) -> Dict[str, np.ndarray]:
    """
    [TSA-2] Extract test-set embeddings, predictions, labels, and cell names from the model.
    """
    tsa_trainer = _create_tsa_trainer(config)

    predictions = tsa_trainer.predict(
        model=model,
        dataloaders=datamodule.test_dataloader(),
    )

    all_embeddings = []
    all_label_preds = []
    all_labels = []
    all_obs_names = []

    for batch_out in predictions:
        all_embeddings.append(batch_out["embedding"].detach().cpu().numpy())
        all_label_preds.append(batch_out["label_pred"].detach().cpu().numpy())

        obs = batch_out["obs_name"]
        if isinstance(obs, (list, tuple)):
            all_obs_names.extend(obs)
        else:
            all_obs_names.extend(list(obs))

        y = batch_out.get("y")
        if y is not None:
            if isinstance(y, torch.Tensor):
                all_labels.append(y.detach().cpu().numpy())
            else:
                all_labels.append(np.array(y))

    result = {
        "embeddings": np.concatenate(all_embeddings, axis=0),
        "label_preds": np.concatenate(all_label_preds, axis=0),
        "obs_names": np.array(all_obs_names),
    }

    if all_labels:
        labels_concat = np.concatenate(all_labels, axis=0)
        if labels_concat.ndim > 1 and labels_concat.shape[1] > 1:
            result["labels"] = np.argmax(labels_concat, axis=1)
        else:
            result["labels"] = labels_concat.astype(np.int64).flatten()
    else:
        result["labels"] = None

    return result


def _metrics_dict_to_yaml_safe(metrics_list: Optional[List[Dict]]) -> Dict[str, float]:
    """Convert the metrics list returned by trainer.test() into a YAML-serializable dict."""
    if not metrics_list or not metrics_list[0]:
        return {}
    result = {}
    for k, v in metrics_list[0].items():
        if isinstance(v, torch.Tensor):
            result[k] = round(float(v.item()), 6)
        elif isinstance(v, (float, int)):
            result[k] = round(float(v), 6)
        else:
            result[k] = v
    return result


def _compute_delta_metrics(
    baseline: Dict[str, float],
    finetuned: Dict[str, float],
) -> Dict[str, Any]:
    """[TSA-1] Compute metric deltas as fine-tuned minus baseline."""
    delta = {}
    common_keys = set(baseline.keys()) & set(finetuned.keys())

    for k in sorted(common_keys):
        b_val = baseline[k]
        f_val = finetuned[k]
        if isinstance(b_val, (int, float)) and isinstance(f_val, (int, float)):
            delta[k] = {
                "baseline": b_val,
                "finetuned": f_val,
                "delta": round(f_val - b_val, 6),
            }

    summary = {}
    for metric_fragment, direction in [
        ("F1_weighted", "higher_better"),
        ("F1_macro", "higher_better"),
        ("AUROC", "higher_better"),
        ("Accuracy", "higher_better"),
        ("recon_MSE", "lower_better"),
        ("loss_unweighted", "lower_better"),
    ]:
        for k, v in delta.items():
            if metric_fragment in k:
                d = v["delta"]
                improved = (d > 0) if direction == "higher_better" else (d < 0)
                summary[metric_fragment] = {
                    "delta": d,
                    "improved": improved,
                    "direction": direction,
                }
                break

    delta["_summary"] = summary
    return delta


def run_tissue_specificity_analysis(
    tissue_name: str,
    pretrained_model: DeepTAN,
    best_ckpt: str,
    datamodule: LightningDataModule,
    cv_weights: torch.Tensor,
    class_weights: Optional[List[float]],
    config: Dict[str, Any],
    tissue_num_classes: Optional[int],
    tissue_log_dir: str,
    finetuned_test_results: Optional[List[Dict]],
    pretrained_class_names: Optional[List[str]] = None,
    tissue_class_names: Optional[List[str]] = None,
    split_id: Optional[str] = None,
):
    """
    [TSA] Generate tissue-specific validation artifacts automatically after fine-tuning.

    New in v5:
        pretrained_class_names: pretraining class-name list used for baseline weight slicing
        tissue_class_names: fine-tuning tissue class-name list used for baseline weight slicing
    New in v6:
        split_id: data-split identifier used for logging
    """
    tsa_dir = os.path.join(tissue_log_dir, "tissue_specificity")
    os.makedirs(tsa_dir, exist_ok=True)

    display_name = f"{tissue_name}/{split_id}" if split_id else tissue_name

    logger.info(f"\n{'='*70}")
    logger.info(f"  TISSUE SPECIFICITY ANALYSIS: {display_name}")
    logger.info(f"  Output: {tsa_dir}")
    logger.info(f"{'='*70}")

    # ====================================================================
    # [TSA-1] Step 1: Pretrained Baseline Evaluation
    # ====================================================================
    logger.info(f"\n  [TSA-1] Evaluating pretrained baseline on test set ...")

    baseline_model = DeepTANFineTune(
        pretrained_model=copy.deepcopy(pretrained_model),
        cv_weights=cv_weights.clone(),
        class_weights=class_weights,
        config=config,
        tissue_num_classes=tissue_num_classes,
        baseline_mode=True,
        pretrained_class_names=pretrained_class_names,
        tissue_class_names=tissue_class_names,
    )
    baseline_model.eval()

    baseline_trainer = _create_tsa_trainer(config)
    baseline_test_results = baseline_trainer.test(
        model=baseline_model,
        dataloaders=datamodule.test_dataloader(),
    )

    baseline_metrics = _metrics_dict_to_yaml_safe(baseline_test_results)
    finetuned_metrics = _metrics_dict_to_yaml_safe(finetuned_test_results)

    with open(os.path.join(tsa_dir, "baseline_metrics.yaml"), "w") as f:
        yaml.dump(baseline_metrics, f, default_flow_style=False, allow_unicode=True)
    with open(os.path.join(tsa_dir, "finetuned_metrics.yaml"), "w") as f:
        yaml.dump(finetuned_metrics, f, default_flow_style=False, allow_unicode=True)

    delta = _compute_delta_metrics(baseline_metrics, finetuned_metrics)
    with open(os.path.join(tsa_dir, "delta_metrics.yaml"), "w") as f:
        yaml.dump(delta, f, default_flow_style=False, allow_unicode=True)

    logger.info(f"  [TSA-1] ✓ Baseline / Fine-tuned / Delta metrics saved")

    if "_summary" in delta:
        logger.info(f"\n  {'─'*50}")
        logger.info(f"  Dimension I — Key Deltas ({display_name}):")
        logger.info(f"  {'─'*50}")
        for metric, info in delta["_summary"].items():
            sign = "+" if info["delta"] > 0 else ""
            status = "✓ improved" if info["improved"] else "✗ degraded"
            logger.info(
                f"    {metric:20s}: Δ = {sign}{info['delta']:.4f}  [{status}]"
            )

    # ====================================================================
    # [TSA-2] Step 2: Embedding Extraction
    # ====================================================================
    logger.info(f"\n  [TSA-2] Extracting cell embeddings ...")

    # ── Pretrained embeddings ───────────────────────────────────────────
    logger.info(f"    Extracting pretrained embeddings ...")
    pretrained_preds = _extract_predictions(baseline_model, datamodule, config)

    np.save(os.path.join(tsa_dir, "embeddings_pretrained.npy"),
            pretrained_preds["embeddings"])
    np.save(os.path.join(tsa_dir, "label_preds_pretrained.npy"),
            pretrained_preds["label_preds"])
    logger.info(
        f"    ✓ Pretrained: {pretrained_preds['embeddings'].shape[0]} cells, "
        f"embedding_dim={pretrained_preds['embeddings'].shape[1]}"
    )

    # ── Fine-tuned embeddings ───────────────────────────────────────────
    logger.info(f"    Extracting fine-tuned embeddings ...")

    finetuned_model = DeepTANFineTune(
        pretrained_model=copy.deepcopy(pretrained_model),
        cv_weights=cv_weights.clone(),
        class_weights=class_weights,
        config=config,
        tissue_num_classes=tissue_num_classes,
    )
    ckpt_data = torch.load(best_ckpt, map_location="cpu")
    finetuned_model.load_state_dict(ckpt_data["state_dict"])
    finetuned_model.eval()

    finetuned_preds = _extract_predictions(finetuned_model, datamodule, config)

    np.save(os.path.join(tsa_dir, "embeddings_finetuned.npy"),
            finetuned_preds["embeddings"])
    np.save(os.path.join(tsa_dir, "label_preds_finetuned.npy"),
            finetuned_preds["label_preds"])
    logger.info(
        f"    ✓ Fine-tuned: {finetuned_preds['embeddings'].shape[0]} cells, "
        f"embedding_dim={finetuned_preds['embeddings'].shape[1]}"
    )

    # ── Shared outputs: true labels and cell names ───────────────────
    np.save(os.path.join(tsa_dir, "cell_obs_names.npy"),
            pretrained_preds["obs_names"])

    if pretrained_preds["labels"] is not None:
        np.save(os.path.join(tsa_dir, "cell_labels.npy"),
                pretrained_preds["labels"])
        n_classes = len(np.unique(pretrained_preds["labels"]))
        logger.info(f"    ✓ Labels: {n_classes} classes, "
                    f"{len(pretrained_preds['labels'])} cells")
    else:
        logger.warning(f"    △ No labels available in test set")

    # ── Consistency check ─────────────────────────────────────────────
    n_pre = pretrained_preds["embeddings"].shape[0]
    n_ft = finetuned_preds["embeddings"].shape[0]
    if n_pre != n_ft:
        logger.error(
            f"    ✗ Cell count mismatch: pretrained={n_pre}, finetuned={n_ft}"
        )
    else:
        logger.info(f"    ✓ Cell count consistent: {n_pre}")

    # ── Save TSA metadata ─────────────────────────────────────────────
    baseline_init_method = "weight_slicing" if (
        pretrained_class_names and tissue_class_names
    ) else "xavier_random"

    tsa_metadata = {
        "tissue_name": tissue_name,
        "split_id": split_id,
        "n_cells_test": int(n_pre),
        "embedding_dim": int(pretrained_preds["embeddings"].shape[1]),
        "num_classes": tissue_num_classes,
        "pretrained_ckpt": config["pretrained_ckpt"],
        "finetuned_ckpt": best_ckpt,
        "baseline_init_method": baseline_init_method,
        "files": {
            "dimension_I": [
                "baseline_metrics.yaml",
                "finetuned_metrics.yaml",
                "delta_metrics.yaml",
            ],
            "dimension_II": (
                "See gene_cv_weights.csv in litdata directory "
                "(generated by Run01)"
            ),
            "dimension_III": [
                "embeddings_pretrained.npy",
                "embeddings_finetuned.npy",
                "label_preds_pretrained.npy",
                "label_preds_finetuned.npy",
                "cell_labels.npy",
                "cell_obs_names.npy",
            ],
        },
        "usage_notes": {
            "embeddings": (
                "Shape [n_cells, embedding_dim]. "
                "Rows are aligned across all .npy files."
            ),
            "labels": (
                "Integer class indices (argmax of one-hot). "
                "Aligned with embedding rows."
            ),
            "label_preds": (
                "Raw logits [n_cells, num_classes]. "
                "Apply softmax for probabilities."
            ),
            "baseline_init_method": (
                "weight_slicing: the baseline classification head is initialized by matching class names to pretrained weights; "
                "the delta reflects the true gain from fine-tuning."
                "xavier_random: the baseline classification head is randomly initialized because the pretraining label file is missing; "
                "classification-metric deltas are for reference only."
            ),
            "recommended_analysis": {
                "dimension_I": (
                    "Compare baseline vs finetuned metrics; "
                    "rank tissues by delta magnitude."
                ),
                "dimension_II": (
                    "Load gene_cv_weights.csv from 6 tissues; "
                    "compute cross-tissue correlation matrix + GO enrichment."
                ),
                "dimension_III": (
                    "UMAP on embeddings_pretrained/finetuned; "
                    "compute silhouette score, ARI with cell_labels."
                ),
            },
        },
    }
    with open(os.path.join(tsa_dir, "tsa_metadata.yaml"), "w") as f:
        yaml.dump(tsa_metadata, f, default_flow_style=False, allow_unicode=True)

    # ── Final summary ─────────────────────────────────────────────────
    logger.info(f"\n  {'─'*50}")
    logger.info(f"  TSA Complete — {display_name}")
    logger.info(f"  Baseline init: {baseline_init_method}")
    logger.info(f"  {'─'*50}")
    logger.info(f"  Output: {tsa_dir}")
    for item in sorted(os.listdir(tsa_dir)):
        sz = os.path.getsize(os.path.join(tsa_dir, item)) / 1024
        logger.info(f"    {item} ({sz:.1f} KB)")

    # Release memory
    del baseline_model, finetuned_model
    del pretrained_preds, finetuned_preds
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    logger.success(f"  [TSA] ✓ Tissue specificity data ready for {display_name}")


