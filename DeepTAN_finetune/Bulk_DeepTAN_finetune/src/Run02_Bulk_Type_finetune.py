def deep_update(base: Dict[str, Any], upd: Dict[str, Any]) -> Dict[str, Any]:
    for k, v in (upd or {}).items():
        if isinstance(v, dict) and isinstance(base.get(k), dict):
            base[k] = deep_update(base[k], v)
        else:
            base[k] = v
    return base


def load_config(path: str) -> Dict[str, Any]:
    cfg = copy.deepcopy(DEFAULT_CONFIG)
    with open(path, "r") as f:
        user_cfg = yaml.safe_load(f) or {}
    cfg = deep_update(cfg, user_cfg)
    # Compatible with the dataset-builder style config where the checkpoint lives under pretrained.checkpoint.
    if not cfg.get("pretrained_ckpt") and isinstance(cfg.get("pretrained"), dict):
        cfg["pretrained_ckpt"] = cfg["pretrained"].get("checkpoint", "")
    return cfg


