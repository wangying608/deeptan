def _is_litdata_dir(path: str) -> bool:
    return (
        os.path.isdir(path)
        and os.path.isfile(os.path.join(path, "expanded_metadata.pkl"))
        and os.path.isdir(os.path.join(path, "trn"))
        and os.path.isdir(os.path.join(path, "val"))
        and os.path.isdir(os.path.join(path, "tst"))
    )


def resolve_litdata_dir(cfg: Dict[str, Any]) -> Tuple[str, str]:
    """Resolve the LitData directory for the single-run workflow.

    Preferred GitHub-facing layout:
        dataset_root/expanded_metadata.pkl
        dataset_root/trn
        dataset_root/val
        dataset_root/tst

    Backward-compatible legacy layout:
        dataset_root/seed_<data_id>/expanded_metadata.pkl
        dataset_root/seed_<data_id>/trn
        dataset_root/seed_<data_id>/val
        dataset_root/seed_<data_id>/tst
    """
    root = str(cfg.get("dataset_root", "") or "")
    if not root:
        raise ValueError("Config field dataset_root is required.")

    data_id = str(cfg.get("data_id", "bulk_run") or "bulk_run")

    if _is_litdata_dir(root):
        return root, data_id

    candidates: List[Tuple[str, str]] = []
    if data_id:
        candidates.append((os.path.join(root, f"seed_{data_id}"), data_id))

    legacy_seeds = list(cfg.get("seeds", []) or [])
    if legacy_seeds:
        sid = str(legacy_seeds[0])
        candidates.append((os.path.join(root, f"seed_{sid}"), sid))

    for candidate, cid in candidates:
        if _is_litdata_dir(candidate):
            logger.warning(
                f"Using legacy LitData subdirectory layout: {candidate}. "
                "For the single-run GitHub workflow, prefer setting dataset_root directly to the directory "
                "that contains trn/val/tst and expanded_metadata.pkl."
            )
            return candidate, cid

    expected = (
        f"Expected either {root}/trn, {root}/val, {root}/tst, {root}/expanded_metadata.pkl "
        "or a legacy dataset_root/seed_<data_id> directory with those files."
    )
    raise FileNotFoundError(expected)


def make_dataloader(seed_dir: str, split: str, cfg: Dict[str, Any]):
    ds = litdata.StreamingDataset(os.path.join(seed_dir, split), max_cache_size="10GB")
    return litdata.StreamingDataLoader(
        ds,
        batch_size=int(cfg["batch_size"]),
        num_workers=int(cfg.get("n_workers", 4)),
        collate_fn=collate_fn,
    )


def run_single_dataset(cfg: Dict[str, Any]) -> Dict[str, float]:
    dataset_dir, data_id = resolve_litdata_dir(cfg)
    meta_path = os.path.join(dataset_dir, "expanded_metadata.pkl")
    with open(meta_path, "rb") as f:
        meta = pickle.load(f)

    pretrained = load_pretrained_model(cfg["pretrained_ckpt"])
    model = TraitBulkDeepTAN(pretrained, meta, dataset_dir, cfg)

    train_loader = make_dataloader(dataset_dir, "trn", cfg)
    val_loader = make_dataloader(dataset_dir, "val", cfg)
    test_loader = make_dataloader(dataset_dir, "tst", cfg)

    out_dir = str(cfg["output_dir"])
    os.makedirs(out_dir, exist_ok=True)
    logger_tb = TensorBoardLogger(save_dir=out_dir, name="tensorboard")

    safe_data_id = re.sub(r"[^A-Za-z0-9_.-]+", "_", str(data_id))
    ckpt_prefix = f"FT16_{safe_data_id}_"
    ckpt = ModelCheckpoint(
        dirpath=out_dir,
        filename=ckpt_prefix + "epoch={epoch:02d}_val_loss={val_loss:.4f}",
        monitor="val/loss",
        mode="min",
        save_top_k=3,
        save_last=False,
        auto_insert_metric_name=False,
    )
    early = EarlyStopping(monitor="val/loss", mode="min", patience=int(cfg["es_patience"]), min_delta=1e-4)
    trainer = Trainer(
        accelerator=cfg["accelerator"],
        devices=cfg["devices"],
        precision=cfg["precision"],
        max_epochs=int(cfg["max_epochs"]),
        min_epochs=int(cfg["min_epochs"]),
        accumulate_grad_batches=int(cfg["accumulate_grad_batches"]),
        gradient_clip_val=float(cfg["gradient_clip_val"]),
        logger=logger_tb,
        callbacks=[ckpt, early, LearningRateMonitor(logging_interval="epoch")],
        log_every_n_steps=10,
        num_sanity_val_steps=int(cfg.get("num_sanity_val_steps", 0)),
        check_val_every_n_epoch=int(cfg.get("check_val_every_n_epoch", 1)),
    )
    trainer.fit(model, train_loader, val_loader)
    best_path = ckpt.best_model_path
    best_score = ckpt.best_model_score
    if not best_path:
        raise RuntimeError(
            "No checkpoint was saved. Check whether validation ran and whether val/loss was logged."
        )
    logger.info(f"Best checkpoint: {best_path}")

    test_result = trainer.test(model, dataloaders=test_loader, ckpt_path=best_path)

    metadata = {
        "task": "Trait-aware BulkExpand-DeepTAN fine-tuning",
        "data_id": data_id,
        "training_seed": int(cfg.get("seed", 42)),
        "dataset_dir": dataset_dir,
        "output_dir": out_dir,
        "best_ckpt": best_path,
        "best_val_loss": float(best_score.item()) if best_score is not None else None,
        "monitor": "val/loss",
        "mode": "min",
        "save_top_k": 3,
        "checkpoint_pattern": ckpt_prefix + "epoch={epoch:02d}_val_loss={val_loss:.4f}.ckpt",
        "pretrained_ckpt": cfg["pretrained_ckpt"],
        "dataset_root": cfg["dataset_root"],
        "test_result": test_result,
        "config": cfg,
    }
    with open(os.path.join(out_dir, "finetune_metadata.pkl"), "wb") as f:
        pickle.dump(metadata, f)

    preds = trainer.predict(model, dataloaders=test_loader, ckpt_path=best_path)

    pred_list, target_list, obs_list = [], [], []
    recon_pred_list, recon_target_list = [], []

    for batch in preds:
        pred_list.extend(batch["label_regression_pred"].numpy().tolist())
        target_list.extend(batch["label_regression_target"].numpy().tolist())
        obs = batch.get("obs_name", [])
        if isinstance(obs, list):
            obs_list.extend([str(x) for x in obs])

        if "reconstruction_pred" in batch and "reconstruction_target" in batch:
            recon_pred_list.extend(batch["reconstruction_pred"].numpy().tolist())
            recon_target_list.extend(batch["reconstruction_target"].numpy().tolist())

    label_metrics = named_regression_metrics(np.array(target_list), np.array(pred_list))
    reconstruction_metrics = (
        named_regression_metrics(np.array(recon_target_list), np.array(recon_pred_list))
        if len(recon_target_list) > 0
        else None
    )

    metrics_payload = {
        "data_id": data_id,
        "training_seed": int(cfg.get("seed", 42)),
        "notes": {
            "label_regression": "FT16 label regression metrics: FT16_pred vs FT16_true on the original label scale.",
            "recon": "Node-level reconstruction metrics: reconstructed observed node x vs true GData.x on the model input expression scale.",
            "PCC": "PCC is the Pearson correlation coefficient.",
            "removed": "Internal train-scale losses such as label_regression_MSE_train_scale are not logged or exported as public metrics.",
        },
        "label_regression": label_metrics,
        "recon": reconstruction_metrics,
    }

    flat_metrics: Dict[str, float] = {}
    flat_metrics.update(flatten_metrics("label_regression", label_metrics))
    flat_metrics.update(flatten_metrics("recon", reconstruction_metrics))

    pl.DataFrame({"obs_name": obs_list, "FT16_true": target_list, "FT16_pred": pred_list}).write_csv(
        os.path.join(out_dir, "test_label_regression_predictions.csv")
    )
    pl.DataFrame({"obs_name": obs_list, "FT16_true": target_list, "FT16_pred": pred_list}).write_csv(
        os.path.join(out_dir, "test_predictions.csv")
    )

    with open(os.path.join(out_dir, "test_label_regression_metrics.json"), "w") as f:
        json.dump({"data_id": data_id, "task": "label_regression", "metrics": label_metrics}, f, indent=2)

    if reconstruction_metrics is not None:
        with open(os.path.join(out_dir, "test_recon_metrics.json"), "w") as f:
            json.dump({"data_id": data_id, "task": "recon", "metrics": reconstruction_metrics}, f, indent=2)
        with open(os.path.join(out_dir, "test_reconstruction_metrics.json"), "w") as f:
            json.dump({"data_id": data_id, "task": "recon", "metrics": reconstruction_metrics}, f, indent=2)

    with open(os.path.join(out_dir, "test_metrics.json"), "w") as f:
        json.dump(metrics_payload, f, indent=2)

    return flat_metrics



