    setup_deeptan_path(cfg["local_deeptan_src"])
    os.makedirs(cfg["output_dir"], exist_ok=True)
    seed_everything(int(cfg.get("seed", 42)), workers=True)

    logger.info("=" * 80)
    logger.info(
        "Trait-aware BulkExpand-DeepTAN single-run fine-tuning | "
        f"data_id={cfg.get('data_id', 'bulk_run')} | training_seed={cfg.get('seed', 42)}"
    )
    flat_metrics = run_single_dataset(cfg)

    df = pl.DataFrame([{"data_id": str(cfg.get("data_id", "bulk_run")), **flat_metrics}])
    df.write_csv(os.path.join(cfg["output_dir"], "summary_metrics.csv"))
    summary = {}
    for col in df.columns:
        if col == "data_id":
            continue
        try:
            vals = df[col].to_numpy().astype(float)
        except Exception:
            continue
        summary[col] = {
            "mean": float(np.nanmean(vals)),
            "std": 0.0,
        }
    with open(os.path.join(cfg["output_dir"], "summary_metrics_mean_std.json"), "w") as f:
        json.dump(summary, f, indent=2)
    logger.success(f"All done. Summary saved to {cfg['output_dir']}")


