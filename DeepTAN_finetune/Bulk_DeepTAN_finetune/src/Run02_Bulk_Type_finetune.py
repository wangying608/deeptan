def _rankdata_average(x: np.ndarray) -> np.ndarray:
    order = np.argsort(x)
    ranks = np.empty_like(order, dtype=np.float64)
    ranks[order] = np.arange(len(x), dtype=np.float64)
    # average ties
    vals, starts, counts = np.unique(x[order], return_index=True, return_counts=True)
    for st, ct in zip(starts, counts):
        if ct > 1:
            idx = order[st : st + ct]
            ranks[idx] = ranks[idx].mean()
    return ranks


def corrcoef_safe(a: np.ndarray, b: np.ndarray) -> float:
    a = np.asarray(a, dtype=float)
    b = np.asarray(b, dtype=float)
    if a.size < 2 or np.std(a) < 1e-12 or np.std(b) < 1e-12:
        return float("nan")
    return float(np.corrcoef(a, b)[0, 1])


def regression_metrics(y_true: np.ndarray, y_pred: np.ndarray) -> Dict[str, float]:
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)
    err = y_pred - y_true
    mse = float(np.mean(err ** 2)) if y_true.size else float("nan")
    mae = float(np.mean(np.abs(err))) if y_true.size else float("nan")
    rmse = float(math.sqrt(mse)) if np.isfinite(mse) else float("nan")
    denom = float(np.sum((y_true - y_true.mean()) ** 2)) if y_true.size else 0.0
    r2 = float(1.0 - np.sum(err ** 2) / denom) if denom > 1e-12 else float("nan")
    pearson = corrcoef_safe(y_true, y_pred)
    spearman = corrcoef_safe(_rankdata_average(y_true), _rankdata_average(y_pred)) if y_true.size else float("nan")
    return {"Pearson": pearson, "Spearman": spearman, "RMSE": rmse, "MAE": mae, "MSE": mse, "R2": r2}


def named_regression_metrics(y_true: np.ndarray, y_pred: np.ndarray) -> Dict[str, float]:
    """Return the comparable regression metrics used in DeepTAN reports.

    The bulk-from-scratch DeepTAN regression table reports MSE, MAE, PCC and
    RMSE for both label prediction and reconstruction.  This helper intentionally
    returns only these four comparable metrics so that fine-tuning outputs do not
    mix final task metrics with internal train-scale losses.
    """
    metrics = regression_metrics(y_true, y_pred)
    return {
        "MSE": metrics["MSE"],
        "MAE": metrics["MAE"],
        "PCC": metrics["Pearson"],
        "RMSE": metrics["RMSE"],
    }


def flatten_metrics(prefix: str, metrics: Optional[Dict[str, float]]) -> Dict[str, float]:
    """Flatten a metric dict into prefix_metric_name keys for CSV summaries."""
    if not metrics:
        return {}
    return {f"{prefix}_{k}": float(v) for k, v in metrics.items()}


