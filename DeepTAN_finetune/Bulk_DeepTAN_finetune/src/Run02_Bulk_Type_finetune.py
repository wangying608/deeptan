def load_pretrained_model(ckpt_path: str):
    from deeptan.graph.model import DeepTAN  # type: ignore

    model_dir = os.path.dirname(ckpt_path)
    hparams_path = os.path.join(model_dir, "version_0", "hparams.yaml")
    kwargs = {"map_location": "cpu"}
    if os.path.exists(hparams_path):
        kwargs["hparams_file"] = hparams_path
    logger.info(f"Loading pretrained DeepTAN: {ckpt_path}")
    model = DeepTAN.load_from_checkpoint(ckpt_path, **kwargs)
    model.eval()
    return model


def find_last_linear(module: nn.Module) -> Tuple[str, nn.Linear]:
    last_name = None
    last_linear = None
    for name, m in module.named_modules():
        if isinstance(m, nn.Linear):
            last_name = name
            last_linear = m
    if last_linear is None or last_name is None:
        raise RuntimeError("No Linear layer found in predictor")
    return last_name, last_linear


def set_submodule(root: nn.Module, path: str, new_module: nn.Module):
    parts = path.split(".") if path else []
    if not parts:
        raise ValueError("Cannot replace root module")
    parent = root
    for p in parts[:-1]:
        parent = parent[int(p)] if p.isdigit() else getattr(parent, p)
    last = parts[-1]
    if last.isdigit():
        parent[int(last)] = new_module
    else:
        setattr(parent, last, new_module)


def initialize_new_embeddings_from_edges(
    old_weight: torch.Tensor,
    meta: Dict[str, Any],
    seed_dir: str,
    noise_scale: float = 0.02,
) -> torch.Tensor:
    expanded_dict = meta["dict_node_names"]
    n_old = int(meta["num_old_nodes"])
    n_all = len(expanded_dict)
    d = old_weight.shape[1]
    new_init = old_weight.mean(dim=0).repeat(n_all - n_old, 1)

    edge_path = os.path.join(seed_dir, "edge_source.parquet")
    if os.path.exists(edge_path):
        edge_df = pl.read_parquet(edge_path)
        old_set = set(range(n_old))
        accum = torch.zeros_like(new_init)
        denom = torch.zeros(n_all - n_old, dtype=torch.float32)
        # Use old-new edges. If src new / dst old or reverse, accumulate old embedding.
        for row in edge_df.select(["src_expanded_idx", "dst_expanded_idx", "edge_weight"]).iter_rows():
            s, t, w = int(row[0]), int(row[1]), float(row[2])
            if s >= n_old and t in old_set:
                j = s - n_old
                accum[j] += old_weight[t] * w
                denom[j] += abs(w)
            elif t >= n_old and s in old_set:
                j = t - n_old
                accum[j] += old_weight[s] * w
                denom[j] += abs(w)
        mask = denom > 1e-12
        if mask.any():
            new_init[mask] = accum[mask] / denom[mask].unsqueeze(1)
        logger.info(f"New embedding graph-neighbor initialization: {int(mask.sum())}/{n_all - n_old} new nodes matched old neighbors")

    std = old_weight.std(dim=0, keepdim=True).clamp(min=1e-6)
    new_init = new_init + torch.randn_like(new_init) * std * noise_scale
    return new_init


