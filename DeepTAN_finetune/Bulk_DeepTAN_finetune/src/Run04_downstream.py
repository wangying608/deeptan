# Community detection and module normalization
# =============================================================================


def detect_modules(G: nx.Graph, resolution: float, seed: int) -> Dict[str, int]:
    if G.number_of_nodes() == 0:
        return {}
    if G.number_of_edges() == 0:
        return {n: 0 for n in G.nodes()}

    try:
        import igraph as ig  # type: ignore
        import leidenalg  # type: ignore

        nodes = list(G.nodes())
        node_to_idx = {n: i for i, n in enumerate(nodes)}
        edges = [(node_to_idx[u], node_to_idx[v]) for u, v in G.edges()]
        weights = [float(G[u][v].get("weight", 1.0)) for u, v in G.edges()]
        ig_graph = ig.Graph(n=len(nodes), edges=edges, directed=False)
        ig_graph.es["weight"] = weights
        part = leidenalg.find_partition(
            ig_graph,
            leidenalg.RBConfigurationVertexPartition,
            weights="weight",
            resolution_parameter=float(resolution),
            seed=int(seed),
        )
        return {nodes[i]: int(cid) for cid, community in enumerate(part) for i in community}
    except Exception:
        pass

    try:
        import community as community_louvain  # type: ignore
        return community_louvain.best_partition(
            G,
            weight="weight",
            resolution=float(resolution),
            random_state=int(seed),
        )
    except Exception:
        pass

    communities = nx.algorithms.community.greedy_modularity_communities(G, weight="weight")
    part = {}
    for cid, comm in enumerate(communities):
        for node in comm:
            part[node] = cid
    return part


def normalize_module_ids(raw_part: Dict[str, int], all_nodes: Iterable[str], min_module_size: int) -> Tuple[Dict[str, str], Dict[str, List[str]]]:
    tmp: Dict[int, List[str]] = defaultdict(list)
    for node in all_nodes:
        node_s = str(node)
        if node_s in raw_part:
            tmp[int(raw_part[node_s])].append(node_s)
        else:
            tmp[-1].append(node_s)

    retained: List[Tuple[int, List[str]]] = []
    small_nodes: List[str] = []
    for cid, nodes in tmp.items():
        nodes = sorted(set(str(n) for n in nodes))
        if cid != -1 and len(nodes) >= int(min_module_size):
            retained.append((cid, nodes))
        else:
            small_nodes.extend(nodes)

    if not retained:
        all_sorted = sorted(set(str(n) for n in all_nodes))
        modules = {"M01": all_sorted}
        module_of = {n: "M01" for n in all_sorted}
        return module_of, modules

    retained.sort(key=lambda x: len(x[1]), reverse=True)
    largest_cid, largest_nodes = retained[0]
    merged_largest = sorted(set(largest_nodes) | set(small_nodes))
    retained[0] = (largest_cid, merged_largest)

    retained.sort(key=lambda x: len(x[1]), reverse=True)
    module_of: Dict[str, str] = {}
    modules: Dict[str, List[str]] = {}
    for rank, (_cid, nodes) in enumerate(retained, start=1):
        mid = f"M{rank:02d}"
        modules[mid] = sorted(set(nodes))
        for n in modules[mid]:
            module_of[n] = mid

    return module_of, modules


