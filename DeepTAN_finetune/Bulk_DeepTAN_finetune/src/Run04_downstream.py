r"""
==============================================================================
Run04: Trait-Aware Bulk Network Downstream 
==============================================================================

Purpose
-------
Run non-plotting downstream  for one trait-aware bulk latent network
extracted by Run03.

Pipeline position
-----------------
Run01: user-defined raw bulk files -> LitData
Run02: LitData -> fine-tuned BulkExpand-DeepTAN checkpoint
Run03: LitData + fine-tuned checkpoint -> trait-aware latent network
Run04: trait-aware latent network -> module / hub / gene-set 

Core  workflow
----------------------
This script performs table-based downstream  only. It does not retrain
the model, re-extract node embeddings, or draw publication figures.

Main steps:
  1. Read the Run03 edge table, usually bulk_trait_edge_table.parquet.
  2. Use abs(weight_col), default abs(w_ft), as the non-negative edge strength.
  3. Select the edge subset used for module detection.
  4. Build an undirected weighted NetworkX graph.
  5. Detect modules with Leiden, Louvain, or greedy modularity fallback.
  6. Merge very small raw communities into the largest retained module.
  7. Identify module hub genes by intra-module weighted degree.
  8. Export module-level gene sets and hub-gene sets for external enrichment.
  9. Save stable analysis tables for downstream biological interpretation and
     optional external visualization.

Recommended usage
-----------------
Using the directory generated by Run03:

    python bulk_network_downstream_analysis.py \
        --network_dir /path/to/network_output/trait_network \
        --out_dir /path/to/downstream_output

Equivalent explicit edge-table mode:

    python bulk_network_downstream_analysis.py \
        --edge_table /path/to/network_output/trait_network/bulk_trait_edge_table.parquet \
        --out_dir /path/to/downstream_output

Output
------
The script creates:

    out_dir/
        tables/
            analysis_edge_table.parquet
            analysis_metadata.yaml
            module_assignment.csv
            module_summary.csv
            module_count_summary.csv
            hub_gene_summary.csv
            module_edges.parquet
        gene_sets/
            module_genes/
            module_hubs/

Notes
-----
The --seed argument controls reproducibility for stochastic community-detection
backends. It is not a bulk data split identifier.

Author: DeepTAN Bulk Downstream Analysis Pipeline
"""

